fn collectArrayOfListItems(allocator: std.mem.Allocator, expr: lir.LExpr) EmitError![]const *const lir.LExpr {
    const app = switch (expr) {
        .App => |value| value,
        else => return error.UnsupportedExpr,
    };
    const callee = switch (app.callee.*) {
        .Var => |value| value,
        else => return error.UnsupportedExpr,
    };
    if (!std.mem.eql(u8, callee.name, "Array.of_list") or app.args.len != 1) return error.UnsupportedExpr;
    var items = std.ArrayList(*const lir.LExpr).empty;
    errdefer items.deinit(allocator);
    try collectLoweredListItems(allocator, app.args[0].*, &items);
    return items.toOwnedSlice(allocator);
}

fn collectLoweredListItems(allocator: std.mem.Allocator, expr: lir.LExpr, items: *std.ArrayList(*const lir.LExpr)) EmitError!void {
    const ctor = switch (expr) {
        .Ctor => |value| value,
        else => return error.UnsupportedExpr,
    };
    if (std.mem.eql(u8, ctor.name, "[]")) {
        if (ctor.args.len != 0) return error.UnsupportedExpr;
        return;
    }
    if (!std.mem.eql(u8, ctor.name, "::") or ctor.args.len != 2) return error.UnsupportedExpr;
    try items.append(allocator, ctor.args[0]);
    try collectLoweredListItems(allocator, ctor.args[1].*, items);
}

fn hasMissingConstructors(allocator: std.mem.Allocator, type_decls: []const lir.LVariantType, match_expr: lir.LMatch, emitted_variants: []const []const u8) bool {
    if (matchHasUnguardedCatchAll(match_expr)) return false;
    const missing = missingConstructorsMessage(allocator, type_decls, match_expr, emitted_variants) catch return false;
    defer allocator.free(missing);
    return missing.len != 0;
}

fn emitNonExhaustivePanic(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    type_decls: []const lir.LVariantType,
    match_expr: lir.LMatch,
    emitted_variants: []const []const u8,
    indent_level: usize,
) EmitError!void {
    const missing = try missingConstructorsMessage(allocator, type_decls, match_expr, emitted_variants);
    defer allocator.free(missing);
    try emitIndent(out, allocator, indent_level);
    if (missing.len == 0) {
        try append(out, allocator, "@compileError(\"ZXCAML: non-exhaustive match: guard failure or missing nested constructor\");\n");
    } else {
        try appendPrint(out, allocator, "@compileError(\"ZXCAML: non-exhaustive match: missing constructors: {f}\");\n", .{std.zig.fmtString(missing)});
    }
}

fn missingConstructorsMessage(allocator: std.mem.Allocator, type_decls: []const lir.LVariantType, match_expr: lir.LMatch, emitted_variants: []const []const u8) EmitError![]const u8 {
    _ = emitted_variants;
    var missing = std.ArrayList(u8).empty;
    errdefer missing.deinit(allocator);

    if (userMatchTypeName(match_expr)) |type_name| {
        for (type_decls) |type_decl| {
            if (!std.mem.eql(u8, type_decl.name, type_name)) continue;
            for (type_decl.variants) |variant| {
                if (try constructorExhaustivelyCoveredByName(allocator, type_decls, match_expr, type_name, variant.name)) continue;
                if (missing.items.len != 0) try append(&missing, allocator, ", ");
                try append(&missing, allocator, variant.name);
            }
            return missing.toOwnedSlice(allocator);
        }
        return missing.toOwnedSlice(allocator);
    }

    const family = builtinMatchFamily(match_expr) orelse return missing.toOwnedSlice(allocator);
    for (family.constructors) |ctor_name| {
        if (try constructorExhaustivelyCoveredByName(allocator, type_decls, match_expr, null, ctor_name)) continue;
        if (missing.items.len != 0) try append(&missing, allocator, ", ");
        try append(&missing, allocator, ctor_name);
    }
    return missing.toOwnedSlice(allocator);
}

const BuiltinFamily = struct {
    constructors: []const []const u8,
};

fn builtinMatchFamily(match_expr: lir.LMatch) ?BuiltinFamily {
    for (match_expr.arms) |arm| {
        switch (arm.pattern) {
            .Ctor => |ctor| if (builtinFamilyForCtor(ctor.name)) |family| return family,
            .Wildcard, .Var, .Tuple, .Record => {},
        }
    }
    return null;
}

fn builtinFamilyForCtor(ctor_name: []const u8) ?BuiltinFamily {
    if (std.mem.eql(u8, ctor_name, "Some") or std.mem.eql(u8, ctor_name, "None")) {
        return .{ .constructors = &.{ "Some", "None" } };
    }
    if (std.mem.eql(u8, ctor_name, "Ok") or std.mem.eql(u8, ctor_name, "Error")) {
        return .{ .constructors = &.{ "Ok", "Error" } };
    }
    if (std.mem.eql(u8, ctor_name, "::") or std.mem.eql(u8, ctor_name, "[]")) {
        return .{ .constructors = &.{ "::", "[]" } };
    }
    return null;
}

fn matchHasUnguardedCatchAll(match_expr: lir.LMatch) bool {
    for (match_expr.arms) |arm| {
        if (arm.guard != null) continue;
        switch (arm.pattern) {
            .Wildcard, .Var => return true,
            .Ctor, .Tuple, .Record => {},
        }
    }
    return false;
}

fn constructorUnconditionallyCovered(match_expr: lir.LMatch, type_name: ?[]const u8, constructor: []const u8) bool {
    for (match_expr.arms) |arm| {
        if (arm.guard != null) continue;
        switch (arm.pattern) {
            .Wildcard, .Var => return true,
            .Ctor => |ctor| {
                const same_type = if (type_name == null and ctor.type_name == null)
                    true
                else if (type_name != null and ctor.type_name != null)
                    std.mem.eql(u8, type_name.?, ctor.type_name.?)
                else
                    false;
                if (same_type and std.mem.eql(u8, ctor.name, constructor) and ctorPayloadsIrrefutable(ctor.args)) return true;
            },
            .Tuple, .Record => {},
        }
    }
    return false;
}

fn constructorExhaustivelyCoveredByName(
    allocator: std.mem.Allocator,
    type_decls: []const lir.LVariantType,
    match_expr: lir.LMatch,
    type_name: ?[]const u8,
    constructor: []const u8,
) EmitError!bool {
    if (constructorUnconditionallyCovered(match_expr, type_name, constructor)) return true;

    var payload_patterns = std.ArrayList(lir.LPattern).empty;
    defer payload_patterns.deinit(allocator);

    for (match_expr.arms) |arm| {
        if (arm.guard != null) continue;
        switch (arm.pattern) {
            .Ctor => |ctor| {
                if (!sameConstructorName(ctor, type_name, constructor)) continue;
                if (ctor.args.len != 1) continue;
                try payload_patterns.append(allocator, ctor.args[0]);
            },
            .Wildcard, .Var, .Tuple, .Record => {},
        }
    }

    if (payload_patterns.items.len == 0) return false;
    return patternsCoverFamily(allocator, type_decls, payload_patterns.items);
}

fn caseUnconditionallyCovered(match_expr: lir.LMatch, selected_ctor: lir.LCtorPattern) bool {
    for (match_expr.arms) |arm| {
        if (arm.guard != null) continue;
        switch (arm.pattern) {
            .Wildcard, .Var => return true,
            .Ctor => |ctor| if (sameConstructor(ctor, selected_ctor) and ctorPayloadsIrrefutable(ctor.args)) return true,
            .Tuple, .Record => {},
        }
    }
    return false;
}

fn caseExhaustivelyCovered(
    allocator: std.mem.Allocator,
    type_decls: []const lir.LVariantType,
    match_expr: lir.LMatch,
    selected_ctor: lir.LCtorPattern,
) EmitError!bool {
    if (caseUnconditionallyCovered(match_expr, selected_ctor)) return true;

    var payload_patterns = std.ArrayList(lir.LPattern).empty;
    defer payload_patterns.deinit(allocator);

    for (match_expr.arms) |arm| {
        if (arm.guard != null) continue;
        switch (arm.pattern) {
            .Ctor => |ctor| {
                if (!sameConstructor(ctor, selected_ctor)) continue;
                if (ctor.args.len != 1) continue;
                try payload_patterns.append(allocator, ctor.args[0]);
            },
            .Wildcard, .Var, .Tuple, .Record => {},
        }
    }

    if (payload_patterns.items.len == 0) return false;
    return patternsCoverFamily(allocator, type_decls, payload_patterns.items);
}

fn patternsCoverFamily(
    allocator: std.mem.Allocator,
    type_decls: []const lir.LVariantType,
    patterns: []const lir.LPattern,
) EmitError!bool {
    for (patterns) |pattern| {
        switch (pattern) {
            .Wildcard, .Var => return true,
            .Ctor, .Tuple, .Record => {},
        }
    }

    const first_ctor = firstCtorPattern(patterns) orelse return false;
    if (first_ctor.type_name) |type_name| {
        const type_decl = findUserTypeDecl(type_decls, type_name) orelse return false;
        for (type_decl.variants) |variant| {
            if (!try constructorCoveredByPatterns(allocator, type_decls, patterns, type_name, variant.name)) return false;
        }
        return true;
    }

    const family = builtinFamilyForCtor(first_ctor.name) orelse return false;
    for (family.constructors) |ctor_name| {
        if (!try constructorCoveredByPatterns(allocator, type_decls, patterns, null, ctor_name)) return false;
    }
    return true;
}

fn constructorCoveredByPatterns(
    allocator: std.mem.Allocator,
    type_decls: []const lir.LVariantType,
    patterns: []const lir.LPattern,
    type_name: ?[]const u8,
    constructor: []const u8,
) EmitError!bool {
    var nested_patterns = std.ArrayList(lir.LPattern).empty;
    defer nested_patterns.deinit(allocator);

    for (patterns) |pattern| {
        switch (pattern) {
            .Wildcard, .Var => return true,
            .Ctor => |ctor| {
                if (!sameConstructorName(ctor, type_name, constructor)) continue;
                if (ctor.args.len == 0) return true;
                if (ctorPayloadsIrrefutable(ctor.args)) return true;
                if (ctor.args.len == 1) try nested_patterns.append(allocator, ctor.args[0]);
            },
            .Tuple, .Record => {},
        }
    }

    if (nested_patterns.items.len == 0) return false;
    return patternsCoverFamily(allocator, type_decls, nested_patterns.items);
}

fn firstCtorPattern(patterns: []const lir.LPattern) ?lir.LCtorPattern {
    for (patterns) |pattern| {
        switch (pattern) {
            .Ctor => |ctor| return ctor,
            .Wildcard, .Var, .Tuple, .Record => {},
        }
    }
    return null;
}

fn sameConstructorName(ctor: lir.LCtorPattern, type_name: ?[]const u8, constructor: []const u8) bool {
    const same_type = if (type_name == null and ctor.type_name == null)
        true
    else if (type_name != null and ctor.type_name != null)
        std.mem.eql(u8, type_name.?, ctor.type_name.?)
    else
        false;
    return same_type and std.mem.eql(u8, ctor.name, constructor);
}

fn caseNeedsBuiltinPayload(match_expr: lir.LMatch, selected_ctor: lir.LCtorPattern) bool {
    if (selected_ctor.type_name != null or selected_ctor.args.len == 0) return false;
    for (match_expr.arms) |arm| {
        switch (arm.pattern) {
            .Ctor => |ctor| {
                if (sameConstructor(ctor, selected_ctor) and patternsNeedSource(ctor.args, arm.body.*, arm.guard)) return true;
            },
            .Wildcard, .Var, .Tuple, .Record => {},
        }
    }
    return false;
}

fn ctorPayloadsIrrefutable(patterns: []const lir.LPattern) bool {
    for (patterns) |pattern| {
        switch (pattern) {
            .Wildcard, .Var, .Tuple, .Record => {},
            .Ctor => return false,
        }
    }
    return true;
}

fn userMatchTypeName(match_expr: lir.LMatch) ?[]const u8 {
    for (match_expr.arms) |arm| {
        switch (arm.pattern) {
            .Ctor => |ctor| if (ctor.type_name) |type_name| return type_name,
            .Wildcard, .Var, .Tuple, .Record => {},
        }
    }
    return null;
}

fn zigTypeName(allocator: std.mem.Allocator, ty: lir.LTy) EmitError![]const u8 {
    return switch (ty) {
        .Int => allocator.dupe(u8, "i64"),
        .Bool => allocator.dupe(u8, "prelude.Bool"),
        .Unit => allocator.dupe(u8, "void"),
        .String => allocator.dupe(u8, "[]const u8"),
        .Var => return error.UnsupportedExpr,
        .Closure => allocator.dupe(u8, "*const prelude.Closure"),
        .Record => |record| blk: {
            if (isAccountRecordTy(record)) break :blk try allocator.dupe(u8, "AccountRuntime.AccountView");
            break :blk try zigUserRecordNameFromRecord(allocator, record);
        },
        .Tuple => |items| blk: {
            var out = std.ArrayList(u8).empty;
            errdefer out.deinit(allocator);
            try append(&out, allocator, "struct { ");
            for (items, 0..) |item, index| {
                if (index != 0) try append(&out, allocator, ", ");
                const item_ty = try zigTypeName(allocator, item);
                defer allocator.free(item_ty);
                try appendPrint(&out, allocator, "@\"{d}\": {s}", .{ index, item_ty });
            }
            try append(&out, allocator, " }");
            break :blk try out.toOwnedSlice(allocator);
        },
        .Adt => |adt| blk: {
            if (std.mem.eql(u8, adt.name, "option")) {
                if (adt.params.len != 1) return error.UnsupportedExpr;
                const inner = try zigTypeName(allocator, adt.params[0]);
                defer allocator.free(inner);
                break :blk try std.fmt.allocPrint(allocator, "prelude.Option({s})", .{inner});
            }
            if (std.mem.eql(u8, adt.name, "result")) {
                if (adt.params.len != 2) return error.UnsupportedExpr;
                const ok_ty = try zigTypeName(allocator, adt.params[0]);
                defer allocator.free(ok_ty);
                const err_ty = try zigTypeName(allocator, adt.params[1]);
                defer allocator.free(err_ty);
                break :blk try std.fmt.allocPrint(allocator, "prelude.Result({s}, {s})", .{ ok_ty, err_ty });
            }
            if (std.mem.eql(u8, adt.name, "list")) {
                if (adt.params.len != 1) return error.UnsupportedExpr;
                const inner = try zigTypeName(allocator, adt.params[0]);
                defer allocator.free(inner);
                break :blk try std.fmt.allocPrint(allocator, "prelude.List({s})", .{inner});
            }
            if (std.mem.eql(u8, adt.name, "array")) {
                if (adt.params.len != 1) return error.UnsupportedExpr;
                const inner = try zigTypeName(allocator, adt.params[0]);
                defer allocator.free(inner);
                break :blk try std.fmt.allocPrint(allocator, "[]const {s}", .{inner});
            }
            break :blk try zigUserAdtNameFromAdt(allocator, adt);
        },
    };
}

fn zigTypeExprName(allocator: std.mem.Allocator, ty: lir.LTypeExpr, type_params: []const []const u8) EmitError![]const u8 {
    return switch (ty) {
        .TypeVar => |name| blk: {
            if (!containsString(type_params, name)) return error.UnsupportedExpr;
            break :blk try typeParamName(allocator, name);
        },
        .TypeRef => |ref| try zigTypeRefName(allocator, ref, type_params),
        .RecursiveRef => |ref| try zigRecursiveTypeRefName(allocator, ref, type_params),
        .Tuple => |items| blk: {
            if (items.len == 0) break :blk try allocator.dupe(u8, "void");
            var out = std.ArrayList(u8).empty;
            errdefer out.deinit(allocator);
            try append(&out, allocator, "struct { ");
            for (items, 0..) |item, index| {
                if (index != 0) try append(&out, allocator, ", ");
                const item_ty = try zigTypeExprName(allocator, item, type_params);
                defer allocator.free(item_ty);
                try appendPrint(&out, allocator, "_{d}: {s}", .{ index, item_ty });
            }
            try append(&out, allocator, " }");
            break :blk try out.toOwnedSlice(allocator);
        },
    };
}

fn zigRecursiveTypeRefName(allocator: std.mem.Allocator, ref: lir.LTypeRef, type_params: []const []const u8) EmitError![]const u8 {
