//! ZigBackend source emission for arena-threaded Lowered IR modules.
//!
//! RESPONSIBILITIES:
//! - Emit readable Zig source from Lowered IR, never Core IR directly.
//! - Preserve the ABI-mandated `omlz_user_entrypoint` function signature.
//! - Keep M0 generated output syntactically valid and `zig fmt` clean.

const std = @import("std");
const lir = @import("../lower/lir.zig");
const match_compiler = @import("match.zig");

/// Source emission errors for the M0 Zig backend.
pub const EmitError = std.mem.Allocator.Error || error{
    NegativeIntegerResultUnsupported,
    UnsupportedCallingConvention,
    UnsupportedExpr,
    InvalidPatternMatrix,
};

/// Emits a complete `.zig` source file for a lowered module.
pub fn emitModule(allocator: std.mem.Allocator, module: lir.LModule) EmitError![]const u8 {
    var out = std.ArrayList(u8).empty;
    errdefer out.deinit(allocator);

    try append(&out, allocator,
        \\// Generated by omlz; do not edit
        \\// Zig 0.16 BPF workaround: before generated code takes the address of
        \\// any module-scope const array, it must copy that array to a local
        \\// stack slot and take the stack copy's address. Otherwise LLVM may
        \\// place the array at low addresses (0x0/0x20) rejected by Solana.
        \\const Arena = @import("runtime/arena.zig").Arena;
        \\const prelude = @import("runtime/prelude.zig");
        \\
        \\
    );
    for (module.type_decls) |type_decl| {
        try emitVariantType(&out, allocator, type_decl);
        try append(&out, allocator, "\n");
    }
    for (module.record_type_decls) |type_decl| {
        try emitRecordType(&out, allocator, type_decl);
        try append(&out, allocator, "\n");
    }
    for (module.functions) |func| {
        try emitFunction(&out, allocator, func, module.type_decls, module.record_type_decls);
        try append(&out, allocator, "\n");
    }
    try emitFunction(&out, allocator, module.entrypoint, module.type_decls, module.record_type_decls);

    return out.toOwnedSlice(allocator);
}

fn emitVariantType(out: *std.ArrayList(u8), allocator: std.mem.Allocator, type_decl: lir.LVariantType) EmitError!void {
    const type_name = try userTypeName(allocator, type_decl.name);
    defer allocator.free(type_name);

    if (type_decl.params.len == 0) {
        try appendPrint(out, allocator, "const {s} = struct {{\n", .{type_name});
    } else {
        try appendPrint(out, allocator, "fn {s}(", .{type_name});
        for (type_decl.params, 0..) |param, index| {
            if (index != 0) try append(out, allocator, ", ");
            const param_name = try typeParamName(allocator, param);
            defer allocator.free(param_name);
            try appendPrint(out, allocator, "comptime {s}: type", .{param_name});
        }
        try append(out, allocator, ") type {\n    return struct {\n");
    }
    try append(out, allocator, "    const Self = @This();\n");
    try append(out, allocator, "    const Tag = enum(u32) { ");
    for (type_decl.variants, 0..) |variant, index| {
        if (index != 0) try append(out, allocator, ", ");
        const variant_name = try userVariantName(allocator, variant.name);
        defer allocator.free(variant_name);
        try appendPrint(out, allocator, "{s} = {d}", .{ variant_name, variant.tag });
    }
    try append(out, allocator, " };\n");

    try append(out, allocator, "    const Payload = union { ");
    var payload_count: usize = 0;
    for (type_decl.variants) |variant| {
        if (variant.payload_types.len == 0) continue;
        if (payload_count != 0) try append(out, allocator, ", ");
        const variant_name = try userVariantName(allocator, variant.name);
        defer allocator.free(variant_name);
        const payload_ty = try payloadTypeName(allocator, variant, type_decl.params);
        defer allocator.free(payload_ty);
        try appendPrint(out, allocator, "{s}: {s}", .{ variant_name, payload_ty });
        payload_count += 1;
    }
    if (payload_count == 0) try append(out, allocator, "_none: void");
    try append(out, allocator, " };\n");
    try append(out, allocator, "    tag: Tag,\n");
    try append(out, allocator, "    payload: Payload = undefined,\n\n");

    for (type_decl.variants) |variant| {
        try emitVariantConstructorHelper(out, allocator, variant, type_decl.params);
    }
    if (type_decl.params.len == 0) {
        try append(out, allocator, "};\n");
    } else {
        try append(out, allocator, "    };\n}\n");
    }
}

fn emitRecordType(out: *std.ArrayList(u8), allocator: std.mem.Allocator, type_decl: lir.LRecordType) EmitError!void {
    const type_name = try userTypeName(allocator, type_decl.name);
    defer allocator.free(type_name);
    try appendPrint(out, allocator, "const {s} = struct {{\n", .{type_name});
    for (type_decl.fields) |field| {
        try append(out, allocator, "    ");
        try emitIdentifier(out, allocator, field.name);
        try append(out, allocator, ": ");
        const ty_name = try zigTypeExprName(allocator, field.ty, type_decl.params);
        defer allocator.free(ty_name);
        try appendPrint(out, allocator, "{s},\n", .{ty_name});
    }
    try append(out, allocator, "};\n");
}

fn emitVariantConstructorHelper(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    variant: lir.LVariantCtor,
    type_params: []const []const u8,
) EmitError!void {
    const variant_name = try userVariantName(allocator, variant.name);
    defer allocator.free(variant_name);

    try appendPrint(out, allocator, "    pub fn {s}(", .{variant_name});
    if (variant.payload_types.len == 1) {
        const payload_ty = try zigTypeExprName(allocator, variant.payload_types[0], type_params);
        defer allocator.free(payload_ty);
        try appendPrint(out, allocator, "value: {s}", .{payload_ty});
    } else if (variant.payload_types.len > 1) {
        const payload_ty = try payloadTypeName(allocator, variant, type_params);
        defer allocator.free(payload_ty);
        try appendPrint(out, allocator, "value: {s}", .{payload_ty});
    }
    try append(out, allocator, ") Self {\n");
    try appendPrint(out, allocator, "        return .{{ .tag = .{s}", .{variant_name});
    if (variant.payload_types.len > 0) {
        try appendPrint(out, allocator, ", .payload = .{{ .{s} = value }}", .{variant_name});
    }
    try append(out, allocator, " };\n");
    try append(out, allocator, "    }\n");
}

fn emitFunction(out: *std.ArrayList(u8), allocator: std.mem.Allocator, func: lir.LFunc, type_decls: []const lir.LVariantType, record_type_decls: []const lir.LRecordType) EmitError!void {
    try append(out, allocator, "// source span: unavailable (M0 frontend bridge does not emit spans yet)\n");
    const is_entrypoint = std.mem.eql(u8, func.name, "entrypoint");
    try append(out, allocator, if (is_entrypoint) "pub fn " else "fn ");
    const function_name = switch (func.calling_convention) {
        .ArenaThreaded => try emittedFunctionName(allocator, func.name),
        .Closure => try emittedClosureFunctionName(allocator, func.name),
    };
    defer freeEmittedFunctionName(allocator, func.name, function_name);
    try append(out, allocator, function_name);
    try append(out, allocator, "(arena: *Arena");
    if (is_entrypoint) {
        try append(out, allocator, ", input: [*]const u8) callconv(.c) u64 {\n");
    } else {
        if (func.calling_convention == .Closure) {
            try append(out, allocator, ", closure: *const prelude.Closure");
        }
        for (func.params) |param| {
            try append(out, allocator, ", ");
            try emitIdentifier(out, allocator, param.name);
            try append(out, allocator, ": ");
            const ty_name = try zigTypeName(allocator, param.ty);
            defer allocator.free(ty_name);
            try append(out, allocator, ty_name);
        }
        try append(out, allocator, ") i64 {\n");
    }
    if (!exprNeedsArena(func.body, type_decls)) {
        try append(out, allocator, "    _ = arena;\n");
    }
    if (is_entrypoint) try append(out, allocator, "    _ = input;\n");
    if (func.calling_convention == .Closure) {
        try emitClosureCaptureBindings(out, allocator, func.captures);
    }
    try append(out, allocator, if (is_entrypoint) "    return @intCast(" else "    return ");
    var ctx: EmitContext = .{ .type_decls = type_decls, .record_type_decls = record_type_decls };
    try emitExpr(out, allocator, func.body, 1, &ctx);
    try append(out, allocator, if (is_entrypoint) ");\n" else ";\n");
    try append(out, allocator, "}\n");
}

fn emittedFunctionName(allocator: std.mem.Allocator, source_name: []const u8) EmitError![]const u8 {
    if (std.mem.eql(u8, source_name, "entrypoint")) {
        return "omlz_user_entrypoint";
    }

    var out = std.ArrayList(u8).empty;
    errdefer out.deinit(allocator);
    try append(&out, allocator, "omlz_user_");
    for (source_name) |ch| {
        if (std.ascii.isAlphanumeric(ch) or ch == '_') {
            try out.append(allocator, ch);
        } else {
            try out.append(allocator, '_');
        }
    }
    return out.toOwnedSlice(allocator);
}

fn emittedClosureFunctionName(allocator: std.mem.Allocator, source_name: []const u8) EmitError![]const u8 {
    const base = try emittedFunctionName(allocator, source_name);
    defer freeEmittedFunctionName(allocator, source_name, base);
    return std.fmt.allocPrint(allocator, "{s}__closure", .{base});
}

fn emitClosureCaptureBindings(out: *std.ArrayList(u8), allocator: std.mem.Allocator, captures: []const lir.LParam) EmitError!void {
    try append(out, allocator, "    _ = closure.self;\n");
    for (captures, 0..) |capture, index| {
        try append(out, allocator, "    const ");
        try emitIdentifier(out, allocator, capture.name);
        try appendPrint(out, allocator, " = closure.captures[{d}].", .{index});
        switch (capture.ty) {
            .Int => try append(out, allocator, "asInt()"),
            .Closure => try append(out, allocator, "asClosure()"),
            else => return error.UnsupportedExpr,
        }
        try append(out, allocator, ";\n");
    }
}

const EmitContext = struct {
    next_block_id: usize = 0,
    type_decls: []const lir.LVariantType = &.{},
    record_type_decls: []const lir.LRecordType = &.{},
};

fn emitExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    expr: lir.LExpr,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    switch (expr) {
        .Constant => |constant| switch (constant) {
            .Int => |value| {
                // Emit integer constants as @as(i64, value) instead of bare
                // literals.  Bare integer literals are comptime_int in Zig;
                // when used inside a labeled block that contains runtime
                // control flow (e.g. if-then-else), the compiler rejects
                // them with "value with comptime-only type depends on
                // runtime control flow".  Wrapping in @as(i64, ...) gives
                // a concrete runtime type that works in all contexts.
                try appendPrint(out, allocator, "@as(i64, {d})", .{value});
            },
            .String => |value| try appendPrint(out, allocator, "\"{f}\"", .{std.zig.fmtString(value)}),
        },
        .Var => |var_ref| try emitIdentifier(out, allocator, var_ref.name),
        .App => |app| try emitAppExpr(out, allocator, app, indent_level, ctx),
        .Let => |let_expr| try emitLetExpr(out, allocator, let_expr, indent_level, ctx),
        .If => |if_expr| try emitIfExpr(out, allocator, if_expr, indent_level, ctx),
        .Prim => |prim| try emitPrimExpr(out, allocator, prim, indent_level, ctx),
        .Ctor => |ctor_expr| try emitCtorExpr(out, allocator, ctor_expr, indent_level, ctx),
        .Match => |match_expr| try emitMatchExpr(out, allocator, match_expr, indent_level, ctx),
        .Closure => |closure| try emitClosureExpr(out, allocator, closure, indent_level, ctx),
        .Tuple => |tuple_expr| try emitTupleExpr(out, allocator, tuple_expr, indent_level, ctx),
        .TupleProj => |tuple_proj| try emitTupleProjExpr(out, allocator, tuple_proj, indent_level, ctx),
        .Record => |record_expr| try emitRecordExpr(out, allocator, record_expr, indent_level, ctx),
        .RecordField => |record_field| try emitRecordFieldExpr(out, allocator, record_field, indent_level, ctx),
        .RecordUpdate => |record_update| try emitRecordUpdateExpr(out, allocator, record_update, indent_level, ctx),
    }
}

fn emitTupleExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    tuple_expr: lir.LTuple,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    _ = tuple_expr.ty;
    try append(out, allocator, ".{ ");
    for (tuple_expr.items, 0..) |item, index| {
        if (index != 0) try append(out, allocator, ", ");
        try appendPrint(out, allocator, ".@\"{d}\" = ", .{index});
        try emitExpr(out, allocator, item.*, indent_level, ctx);
    }
    try append(out, allocator, " }");
}

fn emitTupleProjExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    tuple_proj: lir.LTupleProj,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    try append(out, allocator, "(");
    try emitExpr(out, allocator, tuple_proj.tuple_expr.*, indent_level, ctx);
    try appendPrint(out, allocator, ").@\"{d}\"", .{tuple_proj.index});
}

fn emitRecordExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    record_expr: lir.LRecord,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    const ty_name = try zigTypeName(allocator, record_expr.ty);
    defer allocator.free(ty_name);
    try appendPrint(out, allocator, "{s}{{ ", .{ty_name});
    for (record_expr.fields, 0..) |field, index| {
        if (index != 0) try append(out, allocator, ", ");
        try append(out, allocator, ".");
        try emitIdentifier(out, allocator, field.name);
        try append(out, allocator, " = ");
        try emitExpr(out, allocator, field.value.*, indent_level, ctx);
    }
    try append(out, allocator, " }");
}

fn emitRecordFieldExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    record_field: lir.LRecordField,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    try append(out, allocator, "(");
    try emitExpr(out, allocator, record_field.record_expr.*, indent_level, ctx);
    try append(out, allocator, ").");
    try emitIdentifier(out, allocator, record_field.field_name);
}

fn emitRecordUpdateExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    record_update: lir.LRecordUpdate,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    const record_ty = switch (record_update.ty) {
        .Record => |value| value,
        else => return error.UnsupportedExpr,
    };
    const record_decl = findRecordTypeDecl(ctx.record_type_decls, record_ty.name) orelse return error.UnsupportedExpr;
    const ty_name = try zigTypeName(allocator, record_update.ty);
    defer allocator.free(ty_name);
    const block_id = ctx.next_block_id;
    ctx.next_block_id += 1;
    try appendPrint(out, allocator, "blk{d}: {{\n", .{block_id});
    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "const omlz_record_base_{d} = ", .{block_id});
    try emitExpr(out, allocator, record_update.base_expr.*, indent_level + 1, ctx);
    try append(out, allocator, ";\n");
    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "break :blk{d} {s}{{ ", .{ block_id, ty_name });
    for (record_decl.fields, 0..) |field, index| {
        if (index != 0) try append(out, allocator, ", ");
        try append(out, allocator, ".");
        try emitIdentifier(out, allocator, field.name);
        try append(out, allocator, " = ");
        if (findRecordUpdateField(record_update.fields, field.name)) |updated| {
            try emitExpr(out, allocator, updated.value.*, indent_level + 1, ctx);
        } else {
            try appendPrint(out, allocator, "omlz_record_base_{d}.", .{block_id});
            try emitIdentifier(out, allocator, field.name);
        }
    }
    try append(out, allocator, " };\n");
    try emitIndent(out, allocator, indent_level);
    try append(out, allocator, "}");
}

fn emitAppExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    app: lir.LApp,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    if (app.kind == .Closure) {
        const callee = switch (app.callee.*) {
            .Var => |var_ref| var_ref,
            else => return error.UnsupportedExpr,
        };
        try emitIdentifier(out, allocator, callee.name);
        try append(out, allocator, ".code(arena, ");
        try emitIdentifier(out, allocator, callee.name);
        for (app.args) |arg| {
            try append(out, allocator, ", ");
            try emitExpr(out, allocator, arg.*, indent_level, ctx);
        }
        try append(out, allocator, ")");
        return;
    }

    switch (app.callee.*) {
        .Var => |callee| {
            const function_name = try emittedFunctionName(allocator, callee.name);
            defer freeEmittedFunctionName(allocator, callee.name, function_name);
            try append(out, allocator, function_name);
        },
        else => return error.UnsupportedExpr,
    }
    try append(out, allocator, "(arena");
    for (app.args) |arg| {
        try append(out, allocator, ", ");
        try emitExpr(out, allocator, arg.*, indent_level, ctx);
    }
    try append(out, allocator, ")");
}

fn freeEmittedFunctionName(allocator: std.mem.Allocator, source_name: []const u8, emitted: []const u8) void {
    if (!std.mem.eql(u8, source_name, "entrypoint")) allocator.free(emitted);
}

fn emitIfExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    if_expr: lir.LIf,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    const block_id = ctx.next_block_id;
    ctx.next_block_id += 1;
    try appendPrint(out, allocator, "blk{d}: {{\n", .{block_id});
    try emitIndent(out, allocator, indent_level + 1);
    try append(out, allocator, "if (prelude.Bool.toNative(");
    try emitExpr(out, allocator, if_expr.cond.*, indent_level + 1, ctx);
    try append(out, allocator, ")) {\n");
    try emitIndent(out, allocator, indent_level + 2);
    try appendPrint(out, allocator, "break :blk{d} ", .{block_id});
    try emitExpr(out, allocator, if_expr.then_branch.*, indent_level + 2, ctx);
    try append(out, allocator, ";\n");
    try emitIndent(out, allocator, indent_level + 1);
    try append(out, allocator, "} else {\n");
    try emitIndent(out, allocator, indent_level + 2);
    try appendPrint(out, allocator, "break :blk{d} ", .{block_id});
    try emitExpr(out, allocator, if_expr.else_branch.*, indent_level + 2, ctx);
    try append(out, allocator, ";\n");
    try emitIndent(out, allocator, indent_level + 1);
    try append(out, allocator, "}\n");
    try emitIndent(out, allocator, indent_level);
    try append(out, allocator, "}");
}

fn emitPrimExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    prim: lir.LPrim,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    if (prim.args.len != 2) return error.UnsupportedExpr;
    switch (prim.op) {
        .Div => {
            try append(out, allocator, "prelude.intDiv(");
            try emitExpr(out, allocator, prim.args[0].*, indent_level, ctx);
            try append(out, allocator, ", ");
            try emitExpr(out, allocator, prim.args[1].*, indent_level, ctx);
            try append(out, allocator, ")");
        },
        .Mod => {
            try append(out, allocator, "prelude.intMod(");
            try emitExpr(out, allocator, prim.args[0].*, indent_level, ctx);
            try append(out, allocator, ", ");
            try emitExpr(out, allocator, prim.args[1].*, indent_level, ctx);
            try append(out, allocator, ")");
        },
        .Eq, .Ne, .Lt, .Le, .Gt, .Ge => {
            try append(out, allocator, "prelude.Bool.fromNative((");
            try emitExpr(out, allocator, prim.args[0].*, indent_level, ctx);
            try appendPrint(out, allocator, " {s} ", .{primOpToken(prim.op)});
            try emitExpr(out, allocator, prim.args[1].*, indent_level, ctx);
            try append(out, allocator, "))");
        },
        .Add, .Sub, .Mul => {
            try append(out, allocator, "(");
            try emitExpr(out, allocator, prim.args[0].*, indent_level, ctx);
            try appendPrint(out, allocator, " {s} ", .{primOpToken(prim.op)});
            try emitExpr(out, allocator, prim.args[1].*, indent_level, ctx);
            try append(out, allocator, ")");
        },
    }
}

fn emitMatchExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    match_expr: lir.LMatch,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    const block_id = ctx.next_block_id;
    ctx.next_block_id += 1;
    const scrutinee_name = try std.fmt.allocPrint(allocator, "omlz_match_scrutinee_{d}", .{block_id});
    defer allocator.free(scrutinee_name);

    try appendPrint(out, allocator, "blk{d}: {{\n", .{block_id});
    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "const {s} = ", .{scrutinee_name});
    try emitExpr(out, allocator, match_expr.scrutinee.*, indent_level + 1, ctx);
    try append(out, allocator, ";\n");

    if (!matchHasCtorArm(match_expr)) {
        try emitNonCtorMatch(out, allocator, match_expr, scrutinee_name, block_id, indent_level, ctx);
        try emitIndent(out, allocator, indent_level);
        try append(out, allocator, "}");
        return;
    }

    try emitDecisionMatchArms(out, allocator, match_expr, scrutinee_name, block_id, indent_level + 1, ctx);
    try emitIndent(out, allocator, indent_level);
    try append(out, allocator, "}");
}

fn emitDecisionMatchArms(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    match_expr: lir.LMatch,
    scrutinee_name: []const u8,
    block_id: usize,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    var decision_arena = std.heap.ArenaAllocator.init(allocator);
    defer decision_arena.deinit();
    const decision_allocator = decision_arena.allocator();
    const matrix = try buildDecisionMatrix(decision_allocator, match_expr);
    const actions = try buildDecisionActions(decision_allocator, match_expr.arms.len);
    const tree = try match_compiler.compileMatrix(decision_allocator, matrix, actions);

    switch (tree.*) {
        .Switch => {},
        .Leaf, .Fail => {
            try emitSequentialMatchRows(out, allocator, match_expr, scrutinee_name, block_id, indent_level, ctx);
            return;
        },
    }

    const user_ctor_match = matchHasUserCtorArm(match_expr);
    var emitted = std.ArrayList([]const u8).empty;
    defer {
        for (emitted.items) |name| allocator.free(name);
        emitted.deinit(allocator);
    }

    try emitIndent(out, allocator, indent_level);
    if (user_ctor_match) {
        try appendPrint(out, allocator, "switch ({s}.tag) {{\n", .{scrutinee_name});
    } else {
        try appendPrint(out, allocator, "switch ({s}) {{\n", .{scrutinee_name});
    }

    const switch_node = tree.Switch;
    for (switch_node.cases) |case| {
        const ctor_pattern = findTopLevelCtorPattern(match_expr, case.constructor) orelse continue;
        const variant = try emittedVariantName(allocator, ctor_pattern);
        defer allocator.free(variant);
        if (containsString(emitted.items, variant)) continue;
        try emitted.append(allocator, try allocator.dupe(u8, variant));
        try emitDecisionCase(out, allocator, match_expr, ctor_pattern, variant, scrutinee_name, block_id, indent_level + 1, ctx);
    }

    if (matchHasDefaultRows(match_expr) or hasMissingConstructors(allocator, ctx.type_decls, match_expr, emitted.items)) {
        try emitIndent(out, allocator, indent_level + 1);
        try append(out, allocator, "else => {\n");
        if (matchHasDefaultRows(match_expr)) {
            try emitDefaultRows(out, allocator, match_expr, scrutinee_name, block_id, indent_level + 2, ctx);
        }
        if (!matchHasUnguardedCatchAll(match_expr)) {
            try emitNonExhaustivePanic(out, allocator, ctx.type_decls, match_expr, emitted.items, indent_level + 2);
        }
        try emitIndent(out, allocator, indent_level + 1);
        try append(out, allocator, "},\n");
    }

    try emitIndent(out, allocator, indent_level);
    try append(out, allocator, "}\n");
}

fn buildDecisionMatrix(allocator: std.mem.Allocator, match_expr: lir.LMatch) EmitError![]const []const lir.LPattern {
    const matrix = try allocator.alloc([]const lir.LPattern, match_expr.arms.len);
    for (match_expr.arms, 0..) |arm, index| {
        const row = try allocator.alloc(lir.LPattern, 1);
        row[0] = arm.pattern;
        matrix[index] = row;
    }
    return matrix;
}

fn buildDecisionActions(allocator: std.mem.Allocator, arm_count: usize) EmitError![]const usize {
    const actions = try allocator.alloc(usize, arm_count);
    for (actions, 0..) |*action, index| action.* = index;
    return actions;
}

fn findTopLevelCtorPattern(match_expr: lir.LMatch, constructor: []const u8) ?lir.LCtorPattern {
    for (match_expr.arms) |arm| {
        switch (arm.pattern) {
            .Ctor => |ctor| if (std.mem.eql(u8, ctor.name, constructor)) return ctor,
            .Wildcard, .Var, .Tuple, .Record => {},
        }
    }
    return null;
}

fn emitDecisionCase(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    match_expr: lir.LMatch,
    selected_ctor: lir.LCtorPattern,
    variant: []const u8,
    scrutinee_name: []const u8,
    block_id: usize,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    try emitIndent(out, allocator, indent_level);
    try appendPrint(out, allocator, ".{s}", .{variant});

    const payload_name: ?[]const u8 = if (selected_ctor.type_name == null and selected_ctor.args.len > 0)
        try std.fmt.allocPrint(allocator, "omlz_match_payload_{d}", .{ctx.next_block_id})
    else
        null;
    defer if (payload_name) |name| allocator.free(name);
    if (payload_name != null) ctx.next_block_id += 1;

    if (payload_name) |name| {
        try appendPrint(out, allocator, " => |{s}| {{\n", .{name});
        if (!caseNeedsBuiltinPayload(match_expr, selected_ctor)) {
            try emitIndent(out, allocator, indent_level + 1);
            try appendPrint(out, allocator, "_ = {s};\n", .{name});
        }
    } else {
        try append(out, allocator, " => {\n");
    }

    var case_covered = false;
    for (match_expr.arms) |arm| {
        if (case_covered) break;
        switch (arm.pattern) {
            .Ctor => |ctor_pattern| {
                if (!sameConstructor(ctor_pattern, selected_ctor)) continue;
                try emitSpecializedCtorRow(out, allocator, ctor_pattern, arm.body.*, arm.guard, scrutinee_name, payload_name, block_id, indent_level + 1, ctx);
                if (arm.guard == null and ctorPayloadsIrrefutable(ctor_pattern.args)) case_covered = true;
            },
            .Wildcard, .Var, .Tuple, .Record => {
                const patterns = [_]lir.LPattern{arm.pattern};
                const sources = [_][]const u8{scrutinee_name};
                try emitPatternSequence(out, allocator, patterns[0..], sources[0..], arm.body.*, arm.guard, block_id, indent_level + 1, ctx);
                if (arm.guard == null) case_covered = true;
            },
        }
    }

    const exhaustively_covered = case_covered or try caseExhaustivelyCovered(allocator, ctx.type_decls, match_expr, selected_ctor);
    if (!exhaustively_covered) {
        try emitNonExhaustivePanic(out, allocator, ctx.type_decls, match_expr, &.{}, indent_level + 1);
    } else if (!case_covered) {
        try emitIndent(out, allocator, indent_level + 1);
        try append(out, allocator, "unreachable;\n");
    }
    try emitIndent(out, allocator, indent_level);
    try append(out, allocator, "},\n");
}

fn emitSpecializedCtorRow(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    ctor_pattern: lir.LCtorPattern,
    body: lir.LExpr,
    guard: ?*const lir.LExpr,
    scrutinee_name: []const u8,
    builtin_payload_name: ?[]const u8,
    block_id: usize,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    if (ctor_pattern.type_name != null) {
        const type_name = ctor_pattern.type_name.?;
        const variant_info = findUserVariant(ctx.type_decls, type_name, ctor_pattern.name) orelse return error.UnsupportedExpr;
        if (variant_info.payload_types.len != ctor_pattern.args.len) return error.UnsupportedExpr;
        const variant = try userVariantName(allocator, ctor_pattern.name);
        defer allocator.free(variant);

        var payload_sources = std.ArrayList([]const u8).empty;
        defer {
            for (payload_sources.items) |payload_source| allocator.free(payload_source);
            payload_sources.deinit(allocator);
        }
        for (ctor_pattern.args, variant_info.payload_types, 0..) |_, payload_ty, index| {
            const payload_name = try std.fmt.allocPrint(allocator, "omlz_match_payload_{d}", .{ctx.next_block_id});
            ctx.next_block_id += 1;
            try emitIndent(out, allocator, indent_level);
            try appendPrint(out, allocator, "const {s} = ", .{payload_name});
            if (ctor_pattern.args.len == 1) {
                try appendPrint(out, allocator, "{s}.payload.{s}", .{ scrutinee_name, variant });
            } else {
                try appendPrint(out, allocator, "{s}.payload.{s}._{d}", .{ scrutinee_name, variant, index });
            }
            if (isRecursivePayload(payload_ty)) try append(out, allocator, ".*");
            try append(out, allocator, ";\n");
            if (!patternNeedsSource(ctor_pattern.args[index], body, guard)) {
                try emitIndent(out, allocator, indent_level);
                try appendPrint(out, allocator, "_ = {s};\n", .{payload_name});
            }
            try payload_sources.append(allocator, payload_name);
        }
        try emitPatternSequence(out, allocator, ctor_pattern.args, payload_sources.items, body, guard, block_id, indent_level, ctx);
        return;
    }

    if (ctor_pattern.args.len == 0) {
        try emitGuardedMatchBreak(out, allocator, body, guard, block_id, indent_level, ctx);
        return;
    }
    const payload_name = builtin_payload_name orelse return error.UnsupportedExpr;
    var payload_sources = std.ArrayList([]const u8).empty;
    defer {
        for (payload_sources.items) |payload_source| {
            if (!std.mem.eql(u8, payload_source, payload_name)) allocator.free(payload_source);
        }
        payload_sources.deinit(allocator);
    }
    if (std.mem.eql(u8, ctor_pattern.name, "::")) {
        if (ctor_pattern.args.len != 2) return error.UnsupportedExpr;
        try payload_sources.append(allocator, try std.fmt.allocPrint(allocator, "{s}.head", .{payload_name}));
        try payload_sources.append(allocator, try std.fmt.allocPrint(allocator, "{s}.tail.*", .{payload_name}));
    } else {
        if (ctor_pattern.args.len != 1) return error.UnsupportedExpr;
        try payload_sources.append(allocator, payload_name);
    }
    try emitPatternSequence(out, allocator, ctor_pattern.args, payload_sources.items, body, guard, block_id, indent_level, ctx);
}

fn emitDefaultRows(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    match_expr: lir.LMatch,
    scrutinee_name: []const u8,
    block_id: usize,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    for (match_expr.arms) |arm| {
        switch (arm.pattern) {
            .Wildcard, .Var, .Tuple, .Record => {
                const patterns = [_]lir.LPattern{arm.pattern};
                const sources = [_][]const u8{scrutinee_name};
                try emitPatternSequence(out, allocator, patterns[0..], sources[0..], arm.body.*, arm.guard, block_id, indent_level, ctx);
            },
            .Ctor => {},
        }
    }
}

fn emitPatternSequence(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    patterns: []const lir.LPattern,
    sources: []const []const u8,
    body: lir.LExpr,
    guard: ?*const lir.LExpr,
    block_id: usize,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    if (patterns.len != sources.len) return error.UnsupportedExpr;
    if (patterns.len == 0) {
        try emitGuardedMatchBreak(out, allocator, body, guard, block_id, indent_level, ctx);
        return;
    }

    const pattern = patterns[0];
    const source = sources[0];
    switch (pattern) {
        .Wildcard => {
            try emitIndent(out, allocator, indent_level);
            try append(out, allocator, "{\n");
            try emitPatternSequence(out, allocator, patterns[1..], sources[1..], body, guard, block_id, indent_level + 1, ctx);
            try emitIndent(out, allocator, indent_level);
            try append(out, allocator, "}\n");
        },
        .Var => |name| {
            const should_bind = !std.mem.eql(u8, name, "_") and armUsesName(body, guard, name);
            try emitIndent(out, allocator, indent_level);
            try append(out, allocator, "{\n");
            if (should_bind) {
                try emitIndent(out, allocator, indent_level + 1);
                try append(out, allocator, "const ");
                try emitIdentifier(out, allocator, name);
                try appendPrint(out, allocator, " = {s};\n", .{source});
            }
            try emitPatternSequence(out, allocator, patterns[1..], sources[1..], body, guard, block_id, indent_level + 1, ctx);
            try emitIndent(out, allocator, indent_level);
            try append(out, allocator, "}\n");
        },
        .Ctor => |ctor_pattern| try emitCtorPatternSequence(out, allocator, ctor_pattern, source, patterns[1..], sources[1..], body, guard, block_id, indent_level, ctx),
        .Tuple => |items| {
            var item_sources = std.ArrayList([]const u8).empty;
            defer {
                for (item_sources.items) |item_source| allocator.free(item_source);
                item_sources.deinit(allocator);
            }
            for (items, 0..) |_, index| {
                try item_sources.append(allocator, try std.fmt.allocPrint(allocator, "{s}.@\"{d}\"", .{ source, index }));
            }
            try emitCombinedPatternSequence(out, allocator, items, item_sources.items, patterns[1..], sources[1..], body, guard, block_id, indent_level, ctx);
        },
        .Record => |fields| {
            var field_sources = std.ArrayList([]const u8).empty;
            var field_patterns = std.ArrayList(lir.LPattern).empty;
            defer {
                for (field_sources.items) |field_source| allocator.free(field_source);
                field_sources.deinit(allocator);
                field_patterns.deinit(allocator);
            }
            for (fields) |field| {
                const field_source = try std.fmt.allocPrint(allocator, "{s}.{s}", .{ source, field.name });
                try field_sources.append(allocator, field_source);
                try field_patterns.append(allocator, field.pattern);
            }
            try emitCombinedPatternSequence(out, allocator, field_patterns.items, field_sources.items, patterns[1..], sources[1..], body, guard, block_id, indent_level, ctx);
        },
    }
}

fn emitCtorPatternSequence(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    ctor_pattern: lir.LCtorPattern,
    source: []const u8,
    rest_patterns: []const lir.LPattern,
    rest_sources: []const []const u8,
    body: lir.LExpr,
    guard: ?*const lir.LExpr,
    block_id: usize,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    if (ctor_pattern.type_name != null) {
        try emitUserCtorPatternSequence(out, allocator, ctor_pattern, source, rest_patterns, rest_sources, body, guard, block_id, indent_level, ctx);
        return;
    }

    const variant = try ctorVariantName(ctor_pattern.name);
    try emitIndent(out, allocator, indent_level);
    try appendPrint(out, allocator, "switch ({s}) {{\n", .{source});
    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, ".{s}", .{variant});

    if (ctor_pattern.args.len == 0) {
        try append(out, allocator, " => {\n");
        try emitPatternSequence(out, allocator, rest_patterns, rest_sources, body, guard, block_id, indent_level + 2, ctx);
        try emitIndent(out, allocator, indent_level + 1);
        try append(out, allocator, "},\n");
    } else {
        const payload_name = try std.fmt.allocPrint(allocator, "omlz_match_payload_{d}", .{ctx.next_block_id});
        defer allocator.free(payload_name);
        ctx.next_block_id += 1;
        try appendPrint(out, allocator, " => |{s}| {{\n", .{payload_name});
        if (!patternsNeedSource(ctor_pattern.args, body, guard)) {
            try emitIndent(out, allocator, indent_level + 2);
            try appendPrint(out, allocator, "_ = {s};\n", .{payload_name});
        }

        var payload_sources = std.ArrayList([]const u8).empty;
        defer {
            for (payload_sources.items) |payload_source| {
                if (!std.mem.eql(u8, payload_source, payload_name)) allocator.free(payload_source);
            }
            payload_sources.deinit(allocator);
        }
        if (std.mem.eql(u8, ctor_pattern.name, "::")) {
            if (ctor_pattern.args.len != 2) return error.UnsupportedExpr;
            try payload_sources.append(allocator, try std.fmt.allocPrint(allocator, "{s}.head", .{payload_name}));
            try payload_sources.append(allocator, try std.fmt.allocPrint(allocator, "{s}.tail.*", .{payload_name}));
        } else {
            if (ctor_pattern.args.len != 1) return error.UnsupportedExpr;
            try payload_sources.append(allocator, payload_name);
        }
        try emitCombinedPatternSequence(out, allocator, ctor_pattern.args, payload_sources.items, rest_patterns, rest_sources, body, guard, block_id, indent_level + 2, ctx);
        try emitIndent(out, allocator, indent_level + 1);
        try append(out, allocator, "},\n");
    }
    try emitIndent(out, allocator, indent_level + 1);
    try append(out, allocator, "else => {},\n");
    try emitIndent(out, allocator, indent_level);
    try append(out, allocator, "}\n");
}

fn emitUserCtorPatternSequence(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    ctor_pattern: lir.LCtorPattern,
    source: []const u8,
    rest_patterns: []const lir.LPattern,
    rest_sources: []const []const u8,
    body: lir.LExpr,
    guard: ?*const lir.LExpr,
    block_id: usize,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    const type_name = ctor_pattern.type_name.?;
    const variant_info = findUserVariant(ctx.type_decls, type_name, ctor_pattern.name) orelse return error.UnsupportedExpr;
    if (variant_info.payload_types.len != ctor_pattern.args.len) return error.UnsupportedExpr;
    const variant = try userVariantName(allocator, ctor_pattern.name);
    defer allocator.free(variant);

    try emitIndent(out, allocator, indent_level);
    try appendPrint(out, allocator, "switch ({s}.tag) {{\n", .{source});
    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, ".{s} => {{\n", .{variant});

    var payload_sources = std.ArrayList([]const u8).empty;
    defer {
        for (payload_sources.items) |payload_source| allocator.free(payload_source);
        payload_sources.deinit(allocator);
    }
    for (ctor_pattern.args, variant_info.payload_types, 0..) |_, payload_ty, index| {
        const payload_name = try std.fmt.allocPrint(allocator, "omlz_match_payload_{d}", .{ctx.next_block_id});
        ctx.next_block_id += 1;
        try emitIndent(out, allocator, indent_level + 2);
        try appendPrint(out, allocator, "const {s} = ", .{payload_name});
        if (ctor_pattern.args.len == 1) {
            try appendPrint(out, allocator, "{s}.payload.{s}", .{ source, variant });
        } else {
            try appendPrint(out, allocator, "{s}.payload.{s}._{d}", .{ source, variant, index });
        }
        if (isRecursivePayload(payload_ty)) try append(out, allocator, ".*");
        try append(out, allocator, ";\n");
        if (!patternNeedsSource(ctor_pattern.args[index], body, guard)) {
            try emitIndent(out, allocator, indent_level + 2);
            try appendPrint(out, allocator, "_ = {s};\n", .{payload_name});
        }
        try payload_sources.append(allocator, payload_name);
    }

    try emitCombinedPatternSequence(out, allocator, ctor_pattern.args, payload_sources.items, rest_patterns, rest_sources, body, guard, block_id, indent_level + 2, ctx);
    try emitIndent(out, allocator, indent_level + 1);
    try append(out, allocator, "},\n");
    try emitIndent(out, allocator, indent_level + 1);
    try append(out, allocator, "else => {},\n");
    try emitIndent(out, allocator, indent_level);
    try append(out, allocator, "}\n");
}

fn emitCombinedPatternSequence(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    prefix_patterns: []const lir.LPattern,
    prefix_sources: []const []const u8,
    rest_patterns: []const lir.LPattern,
    rest_sources: []const []const u8,
    body: lir.LExpr,
    guard: ?*const lir.LExpr,
    block_id: usize,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    if (prefix_patterns.len != prefix_sources.len or rest_patterns.len != rest_sources.len) return error.UnsupportedExpr;
    const total = prefix_patterns.len + rest_patterns.len;
    const combined_patterns = try allocator.alloc(lir.LPattern, total);
    defer allocator.free(combined_patterns);
    const combined_sources = try allocator.alloc([]const u8, total);
    defer allocator.free(combined_sources);
    for (prefix_patterns, 0..) |pattern, index| {
        combined_patterns[index] = pattern;
        combined_sources[index] = prefix_sources[index];
    }
    for (rest_patterns, 0..) |pattern, index| {
        combined_patterns[prefix_patterns.len + index] = pattern;
        combined_sources[prefix_patterns.len + index] = rest_sources[index];
    }
    try emitPatternSequence(out, allocator, combined_patterns, combined_sources, body, guard, block_id, indent_level, ctx);
}

fn emitGuardedMatchBreak(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    body: lir.LExpr,
    guard: ?*const lir.LExpr,
    block_id: usize,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    if (guard) |guard_expr| {
        try emitIndent(out, allocator, indent_level);
        try append(out, allocator, "if (prelude.Bool.toNative(");
        try emitExpr(out, allocator, guard_expr.*, indent_level, ctx);
        try append(out, allocator, ")) {\n");
        try emitIndent(out, allocator, indent_level + 1);
        try appendPrint(out, allocator, "break :blk{d} ", .{block_id});
        try emitExpr(out, allocator, body, indent_level + 1, ctx);
        try append(out, allocator, ";\n");
        try emitIndent(out, allocator, indent_level);
        try append(out, allocator, "}\n");
        return;
    }
    try emitIndent(out, allocator, indent_level);
    try appendPrint(out, allocator, "break :blk{d} ", .{block_id});
    try emitExpr(out, allocator, body, indent_level, ctx);
    try append(out, allocator, ";\n");
}

fn emitNonCtorMatch(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    match_expr: lir.LMatch,
    scrutinee_name: []const u8,
    block_id: usize,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    try emitSequentialMatchRows(out, allocator, match_expr, scrutinee_name, block_id, indent_level, ctx);
}

fn emitSequentialMatchRows(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    match_expr: lir.LMatch,
    scrutinee_name: []const u8,
    block_id: usize,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    if (match_expr.arms.len == 0) return error.UnsupportedExpr;

    if (!matchRowsNeedScrutinee(match_expr)) {
        try emitIndent(out, allocator, indent_level + 1);
        try appendPrint(out, allocator, "_ = {s};\n", .{scrutinee_name});
    }

    for (match_expr.arms) |arm| {
        const patterns = [_]lir.LPattern{arm.pattern};
        const sources = [_][]const u8{scrutinee_name};
        try emitPatternSequence(out, allocator, patterns[0..], sources[0..], arm.body.*, arm.guard, block_id, indent_level + 1, ctx);
    }

    if (!matchHasUnguardedCatchAll(match_expr) and !matchHasCtorArm(match_expr)) {
        try emitNonExhaustivePanic(out, allocator, ctx.type_decls, match_expr, &.{}, indent_level + 1);
    }
}

fn matchRowsNeedScrutinee(match_expr: lir.LMatch) bool {
    for (match_expr.arms) |arm| {
        if (patternNeedsSource(arm.pattern, arm.body.*, arm.guard)) return true;
    }
    return false;
}

fn emitCtorExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    ctor_expr: lir.LCtor,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    if (std.mem.eql(u8, ctor_expr.name, "true") or std.mem.eql(u8, ctor_expr.name, "false")) {
        try appendPrint(out, allocator, "prelude.Bool.fromNative({s})", .{ctor_expr.name});
        return;
    }
    const ty_name = if (ctor_expr.type_name) |type_name|
        try zigUserAdtTypeName(allocator, ctor_expr.ty, type_name)
    else
        try zigTypeName(allocator, ctor_expr.ty);
    defer allocator.free(ty_name);
    const variant = if (ctor_expr.type_name != null)
        try userVariantName(allocator, ctor_expr.name)
    else
        try allocator.dupe(u8, try ctorVariantName(ctor_expr.name));
    defer allocator.free(variant);

    if (ctor_expr.type_name != null) {
        if (ctor_expr.args.len == 0) {
            try appendPrint(out, allocator, "{s}{{ .tag = .{s} }}", .{ ty_name, variant });
            return;
        }
        const type_name = ctor_expr.type_name.?;
        const type_decl = findUserTypeDecl(ctx.type_decls, type_name) orelse return error.UnsupportedExpr;
        const variant_info = findUserVariant(ctx.type_decls, type_name, ctor_expr.name) orelse return error.UnsupportedExpr;
        if (variant_info.payload_types.len != ctor_expr.args.len) return error.UnsupportedExpr;

        var needs_block = ctor_expr.args.len > 1;
        for (variant_info.payload_types) |payload_ty| {
            if (isRecursivePayload(payload_ty)) needs_block = true;
        }

        if (!needs_block) {
            try appendPrint(out, allocator, "{s}{{ .tag = .{s}, .payload = .{{ .{s} = ", .{ ty_name, variant, variant });
            try emitExpr(out, allocator, ctor_expr.args[0].*, indent_level, ctx);
            try append(out, allocator, " } }");
            return;
        }

        const block_id = ctx.next_block_id;
        ctx.next_block_id += 1;
        try appendPrint(out, allocator, "blk{d}: {{\n", .{block_id});
        for (ctor_expr.args, variant_info.payload_types, 0..) |arg, payload_ty, index| {
            if (!isRecursivePayload(payload_ty)) continue;
            const ref = switch (payload_ty) {
                .RecursiveRef => |value| value,
                else => unreachable,
            };
            const child_ty_name = try zigInstantiatedTypeRefName(allocator, ref, type_decl.params, ctor_expr.ty);
            defer allocator.free(child_ty_name);
            try emitIndent(out, allocator, indent_level + 1);
            try appendPrint(out, allocator, "const omlz_recursive_payload_{d}_{d} = arena.allocOneOrTrap({s});\n", .{ block_id, index, child_ty_name });
            try emitIndent(out, allocator, indent_level + 1);
            try appendPrint(out, allocator, "omlz_recursive_payload_{d}_{d}.* = ", .{ block_id, index });
            try emitExpr(out, allocator, arg.*, indent_level + 1, ctx);
            try append(out, allocator, ";\n");
        }
        try emitIndent(out, allocator, indent_level + 1);
        try appendPrint(out, allocator, "break :blk{d} {s}{{ .tag = .{s}, .payload = .{{ .{s} = ", .{ block_id, ty_name, variant, variant });
        if (ctor_expr.args.len == 1) {
            if (isRecursivePayload(variant_info.payload_types[0])) {
                try appendPrint(out, allocator, "omlz_recursive_payload_{d}_0", .{block_id});
            } else {
                try emitExpr(out, allocator, ctor_expr.args[0].*, indent_level + 1, ctx);
            }
        } else {
            try append(out, allocator, ".{ ");
            for (ctor_expr.args, variant_info.payload_types, 0..) |arg, payload_ty, index| {
                if (index != 0) try append(out, allocator, ", ");
                try appendPrint(out, allocator, "._{d} = ", .{index});
                if (isRecursivePayload(payload_ty)) {
                    try appendPrint(out, allocator, "omlz_recursive_payload_{d}_{d}", .{ block_id, index });
                } else {
                    try emitExpr(out, allocator, arg.*, indent_level + 1, ctx);
                }
            }
            try append(out, allocator, " }");
        }
        try append(out, allocator, " } };\n");
        try emitIndent(out, allocator, indent_level);
        try append(out, allocator, "}");
        return;
    }

    if (ctor_expr.args.len == 0) {
        try appendPrint(out, allocator, "{s}.{s}", .{ ty_name, variant });
        return;
    }

    if (std.mem.eql(u8, ctor_expr.name, "::")) {
        try emitConsCtorExpr(out, allocator, ctor_expr, ty_name, indent_level, ctx);
        return;
    }

    if (ctor_expr.args.len != 1) return error.UnsupportedExpr;

    const block_id = ctx.next_block_id;
    ctx.next_block_id += 1;
    try appendPrint(out, allocator, "blk{d}: {{\n", .{block_id});

    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "const omlz_ctor_payload_{d} = {s}{{ .{s} = ", .{ block_id, ty_name, variant });
    try emitExpr(out, allocator, ctor_expr.args[0].*, indent_level + 1, ctx);
    try append(out, allocator, " };\n");

    switch (ctor_expr.layout.region) {
        .Arena => {
            try emitIndent(out, allocator, indent_level + 1);
            try appendPrint(out, allocator, "const omlz_ctor_box_{d} = arena.alloc({s}, 1) catch unreachable;\n", .{ block_id, ty_name });
            try emitIndent(out, allocator, indent_level + 1);
            try appendPrint(out, allocator, "omlz_ctor_box_{d}[0] = omlz_ctor_payload_{d};\n", .{ block_id, block_id });
            try emitIndent(out, allocator, indent_level + 1);
            try appendPrint(out, allocator, "break :blk{d} omlz_ctor_box_{d}[0];\n", .{ block_id, block_id });
        },
        .Static, .Stack => {
            try emitIndent(out, allocator, indent_level + 1);
            try appendPrint(out, allocator, "break :blk{d} omlz_ctor_payload_{d};\n", .{ block_id, block_id });
        },
    }

    try emitIndent(out, allocator, indent_level);
    try append(out, allocator, "}");
}

fn emitClosureExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    closure: lir.LClosure,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    const block_id = ctx.next_block_id;
    ctx.next_block_id += 1;
    const code_name = try emittedClosureFunctionName(allocator, closure.name);
    defer allocator.free(code_name);

    try appendPrint(out, allocator, "blk{d}: {{\n", .{block_id});
    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "const omlz_closure_captures_{d} = arena.alloc(prelude.ClosureValue, {d}) catch unreachable;\n", .{ block_id, closure.captures.len });
    for (closure.captures, 0..) |capture, index| {
        try emitIndent(out, allocator, indent_level + 1);
        try appendPrint(out, allocator, "omlz_closure_captures_{d}[{d}] = ", .{ block_id, index });
        switch (capture.ty) {
            .Int => {
                try append(out, allocator, "prelude.ClosureValue.fromInt(");
                try emitIdentifier(out, allocator, capture.name);
                try append(out, allocator, ")");
            },
            .Closure => {
                try append(out, allocator, "prelude.ClosureValue.fromClosure(");
                try emitIdentifier(out, allocator, capture.name);
                try append(out, allocator, ")");
            },
            else => return error.UnsupportedExpr,
        }
        try append(out, allocator, ";\n");
    }
    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "const omlz_closure_slot_{d} = arena.alloc(prelude.Closure, 1) catch unreachable;\n", .{block_id});
    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "omlz_closure_slot_{d}[0] = prelude.Closure.init({s}, omlz_closure_captures_{d});\n", .{ block_id, code_name, block_id });
    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "omlz_closure_slot_{d}[0].self = &omlz_closure_slot_{d}[0];\n", .{ block_id, block_id });
    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "break :blk{d} &omlz_closure_slot_{d}[0];\n", .{ block_id, block_id });
    try emitIndent(out, allocator, indent_level);
    try append(out, allocator, "}");
}

fn emitConsCtorExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    ctor_expr: lir.LCtor,
    ty_name: []const u8,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    if (ctor_expr.args.len != 2) return error.UnsupportedExpr;

    const block_id = ctx.next_block_id;
    ctx.next_block_id += 1;
    try appendPrint(out, allocator, "blk{d}: {{\n", .{block_id});

    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "const omlz_list_head_{d} = ", .{block_id});
    try emitExpr(out, allocator, ctor_expr.args[0].*, indent_level + 1, ctx);
    try append(out, allocator, ";\n");

    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "const omlz_list_tail_{d} = ", .{block_id});
    try emitExpr(out, allocator, ctor_expr.args[1].*, indent_level + 1, ctx);
    try append(out, allocator, ";\n");

    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "const omlz_list_tail_box_{d} = {s}.Box(arena, omlz_list_tail_{d});\n", .{ block_id, ty_name, block_id });

    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "const omlz_ctor_payload_{d} = {s}{{ .cons = .{{ .head = omlz_list_head_{d}, .tail = omlz_list_tail_box_{d} }} }};\n", .{ block_id, ty_name, block_id, block_id });

    switch (ctor_expr.layout.region) {
        .Arena => {
            try emitIndent(out, allocator, indent_level + 1);
            try appendPrint(out, allocator, "const omlz_ctor_box_{d} = arena.alloc({s}, 1) catch unreachable;\n", .{ block_id, ty_name });
            try emitIndent(out, allocator, indent_level + 1);
            try appendPrint(out, allocator, "omlz_ctor_box_{d}[0] = omlz_ctor_payload_{d};\n", .{ block_id, block_id });
            try emitIndent(out, allocator, indent_level + 1);
            try appendPrint(out, allocator, "break :blk{d} omlz_ctor_box_{d}[0];\n", .{ block_id, block_id });
        },
        .Static, .Stack => {
            try emitIndent(out, allocator, indent_level + 1);
            try appendPrint(out, allocator, "break :blk{d} omlz_ctor_payload_{d};\n", .{ block_id, block_id });
        },
    }

    try emitIndent(out, allocator, indent_level);
    try append(out, allocator, "}");
}

fn emitLetExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    let_expr: lir.LLet,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    const block_id = ctx.next_block_id;
    ctx.next_block_id += 1;

    try appendPrint(out, allocator, "blk{d}: {{\n", .{block_id});
    try emitIndent(out, allocator, indent_level + 1);
    const is_discard = std.mem.eql(u8, let_expr.name, "_");
    if (is_discard) {
        try append(out, allocator, "_ = ");
        try emitExpr(out, allocator, let_expr.value.*, indent_level + 1, ctx);
        try append(out, allocator, ";\n");
    } else {
        try append(out, allocator, "const ");
        try emitIdentifier(out, allocator, let_expr.name);
        try append(out, allocator, " = ");
        try emitExpr(out, allocator, let_expr.value.*, indent_level + 1, ctx);
        try append(out, allocator, ";\n");
    }
    if (!is_discard and !exprUsesName(let_expr.body.*, let_expr.name)) {
        try emitIndent(out, allocator, indent_level + 1);
        try append(out, allocator, "_ = ");
        try emitIdentifier(out, allocator, let_expr.name);
        try append(out, allocator, ";\n");
    }
    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "break :blk{d} ", .{block_id});
    try emitExpr(out, allocator, let_expr.body.*, indent_level + 1, ctx);
    try append(out, allocator, ";\n");
    try emitIndent(out, allocator, indent_level);
    try append(out, allocator, "}");
}

fn exprUsesName(expr: lir.LExpr, name: []const u8) bool {
    return switch (expr) {
        .Constant => false,
        .Var => |var_ref| std.mem.eql(u8, var_ref.name, name),
        .App => |app| blk: {
            if (exprUsesName(app.callee.*, name)) break :blk true;
            for (app.args) |arg| {
                if (exprUsesName(arg.*, name)) break :blk true;
            }
            break :blk false;
        },
        .Let => |let_expr| exprUsesName(let_expr.value.*, name) or
            (!std.mem.eql(u8, let_expr.name, name) and exprUsesName(let_expr.body.*, name)),
        .If => |if_expr| exprUsesName(if_expr.cond.*, name) or exprUsesName(if_expr.then_branch.*, name) or exprUsesName(if_expr.else_branch.*, name),
        .Prim => |prim| blk: {
            for (prim.args) |arg| {
                if (exprUsesName(arg.*, name)) break :blk true;
            }
            break :blk false;
        },
        .Ctor => |ctor_expr| {
            for (ctor_expr.args) |arg| {
                if (exprUsesName(arg.*, name)) return true;
            }
            return false;
        },
        .Tuple => |tuple_expr| {
            for (tuple_expr.items) |item| {
                if (exprUsesName(item.*, name)) return true;
            }
            return false;
        },
        .TupleProj => |tuple_proj| exprUsesName(tuple_proj.tuple_expr.*, name),
        .Record => |record_expr| {
            for (record_expr.fields) |field| {
                if (exprUsesName(field.value.*, name)) return true;
            }
            return false;
        },
        .RecordField => |record_field| exprUsesName(record_field.record_expr.*, name),
        .RecordUpdate => |record_update| {
            if (exprUsesName(record_update.base_expr.*, name)) return true;
            for (record_update.fields) |field| {
                if (exprUsesName(field.value.*, name)) return true;
            }
            return false;
        },
        .Match => |match_expr| {
            if (exprUsesName(match_expr.scrutinee.*, name)) return true;
            for (match_expr.arms) |arm| {
                if (!patternBindsName(arm.pattern, name)) {
                    if (arm.guard) |guard_expr| {
                        if (exprUsesName(guard_expr.*, name)) return true;
                    }
                    if (exprUsesName(arm.body.*, name)) return true;
                }
            }
            return false;
        },
        .Closure => |closure| {
            for (closure.captures) |capture| {
                if (std.mem.eql(u8, capture.name, name)) return true;
            }
            return false;
        },
    };
}

fn exprNeedsArena(expr: lir.LExpr, type_decls: []const lir.LVariantType) bool {
    return switch (expr) {
        .Constant, .Var => false,
        .App => true,
        .Let => |let_expr| exprNeedsArena(let_expr.value.*, type_decls) or exprNeedsArena(let_expr.body.*, type_decls),
        .If => |if_expr| exprNeedsArena(if_expr.cond.*, type_decls) or exprNeedsArena(if_expr.then_branch.*, type_decls) or exprNeedsArena(if_expr.else_branch.*, type_decls),
        .Prim => |prim| blk: {
            for (prim.args) |arg| {
                if (exprNeedsArena(arg.*, type_decls)) break :blk true;
            }
            break :blk false;
        },
        .Match => |match_expr| blk: {
            if (exprNeedsArena(match_expr.scrutinee.*, type_decls)) break :blk true;
            for (match_expr.arms) |arm| {
                if (arm.guard) |guard_expr| {
                    if (exprNeedsArena(guard_expr.*, type_decls)) break :blk true;
                }
                if (exprNeedsArena(arm.body.*, type_decls)) break :blk true;
            }
            break :blk false;
        },
        .Ctor => |ctor_expr| blk: {
            if (ctor_expr.type_name) |type_name| {
                const variant = findUserVariant(type_decls, type_name, ctor_expr.name);
                if (variant) |variant_info| {
                    for (variant_info.payload_types) |payload_ty| {
                        if (isRecursivePayload(payload_ty)) break :blk true;
                    }
                }
            } else if (ctor_expr.layout.region == .Arena) {
                break :blk true;
            }
            for (ctor_expr.args) |arg| {
                if (exprNeedsArena(arg.*, type_decls)) break :blk true;
            }
            break :blk false;
        },
        .Tuple => |tuple_expr| blk: {
            for (tuple_expr.items) |item| {
                if (exprNeedsArena(item.*, type_decls)) break :blk true;
            }
            break :blk false;
        },
        .TupleProj => |tuple_proj| exprNeedsArena(tuple_proj.tuple_expr.*, type_decls),
        .Record => |record_expr| blk: {
            for (record_expr.fields) |field| {
                if (exprNeedsArena(field.value.*, type_decls)) break :blk true;
            }
            break :blk false;
        },
        .RecordField => |record_field| exprNeedsArena(record_field.record_expr.*, type_decls),
        .RecordUpdate => |record_update| blk: {
            if (exprNeedsArena(record_update.base_expr.*, type_decls)) break :blk true;
            for (record_update.fields) |field| {
                if (exprNeedsArena(field.value.*, type_decls)) break :blk true;
            }
            break :blk false;
        },
        .Closure => true,
    };
}

fn patternBindsName(pattern: lir.LPattern, name: []const u8) bool {
    return switch (pattern) {
        .Wildcard => false,
        .Var => |var_name| std.mem.eql(u8, var_name, name),
        .Ctor => |ctor_pattern| {
            for (ctor_pattern.args) |arg| {
                if (patternBindsName(arg, name)) return true;
            }
            return false;
        },
        .Tuple => |items| {
            for (items) |item| {
                if (patternBindsName(item, name)) return true;
            }
            return false;
        },
        .Record => |fields| {
            for (fields) |field| {
                if (patternBindsName(field.pattern, name)) return true;
            }
            return false;
        },
    };
}

fn armUsesName(body: lir.LExpr, guard: ?*const lir.LExpr, name: []const u8) bool {
    if (guard) |guard_expr| {
        if (exprUsesName(guard_expr.*, name)) return true;
    }
    return exprUsesName(body, name);
}

fn patternsNeedSource(patterns: []const lir.LPattern, body: lir.LExpr, guard: ?*const lir.LExpr) bool {
    for (patterns) |pattern| {
        if (patternNeedsSource(pattern, body, guard)) return true;
    }
    return false;
}

fn patternNeedsSource(pattern: lir.LPattern, body: lir.LExpr, guard: ?*const lir.LExpr) bool {
    return switch (pattern) {
        .Wildcard => false,
        .Var => |name| !std.mem.eql(u8, name, "_") and armUsesName(body, guard, name),
        .Ctor, .Tuple, .Record => true,
    };
}

