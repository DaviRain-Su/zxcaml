//! ZigBackend source emission for arena-threaded Lowered IR modules.
//!
//! RESPONSIBILITIES:
//! - Emit readable Zig source from Lowered IR, never Core IR directly.
//! - Preserve the ABI-mandated `omlz_user_entrypoint` function signature.
//! - Keep M0 generated output syntactically valid and `zig fmt` clean.

const std = @import("std");
const lir = @import("../lower/lir.zig");
const match_compiler = @import("match.zig");

/// Source emission errors for the M0 Zig backend.
pub const EmitError = std.mem.Allocator.Error || error{
    NegativeIntegerResultUnsupported,
    UnsupportedCallingConvention,
    UnsupportedExpr,
    InvalidPatternMatrix,
};

/// Emits a complete `.zig` source file for a lowered module.
pub fn emitModule(allocator: std.mem.Allocator, module: lir.LModule) EmitError![]const u8 {
    var out = std.ArrayList(u8).empty;
    errdefer out.deinit(allocator);

    try append(&out, allocator,
        \\// Generated by omlz; do not edit
        \\// Zig 0.16 BPF workaround: before generated code takes the address of
        \\// any module-scope const array, it must copy that array to a local
        \\// stack slot and take the stack copy's address. Otherwise LLVM may
        \\// place the array at low addresses (0x0/0x20) rejected by Solana.
        \\const Arena = @import("runtime/arena.zig").Arena;
        \\const AccountRuntime = @import("runtime/account.zig");
        \\const cpi = @import("runtime/cpi.zig");
        \\const prelude = @import("runtime/prelude.zig");
        \\const spl_token = @import("runtime/spl_token.zig");
        \\const syscalls = @import("runtime/syscalls.zig");
        \\
        \\
    );
    for (module.type_decls) |type_decl| {
        try emitVariantType(&out, allocator, type_decl);
        try append(&out, allocator, "\n");
    }
    const has_account_record = hasAccountRecordType(module.record_type_decls);
    const needs_account_support = has_account_record or paramsNeedEntrypointAccounts(module.entrypoint.params);
    for (module.record_type_decls) |type_decl| {
        try emitRecordType(&out, allocator, type_decl);
        try append(&out, allocator, "\n");
    }
    if (!has_account_record and needs_account_support) {
        try emitBuiltinAccountRecordType(&out, allocator);
        try append(&out, allocator, "\n");
    }
    if (needs_account_support) {
        try emitAccountViewHelper(&out, allocator);
        try append(&out, allocator, "\n");
    }
    try emitClosureCaptureStructs(&out, allocator, module.functions);
    for (module.functions) |func| {
        try emitFunction(&out, allocator, func, module.functions, module.type_decls, module.record_type_decls);
        try append(&out, allocator, "\n");
    }
    try emitFunction(&out, allocator, module.entrypoint, module.functions, module.type_decls, module.record_type_decls);

    return out.toOwnedSlice(allocator);
}

fn emitVariantType(out: *std.ArrayList(u8), allocator: std.mem.Allocator, type_decl: lir.LVariantType) EmitError!void {
    const type_name = try userTypeName(allocator, type_decl.name);
    defer allocator.free(type_name);

    if (type_decl.params.len == 0) {
        try appendPrint(out, allocator, "const {s} = struct {{\n", .{type_name});
    } else {
        try appendPrint(out, allocator, "fn {s}(", .{type_name});
        for (type_decl.params, 0..) |param, index| {
            if (index != 0) try append(out, allocator, ", ");
            const param_name = try typeParamName(allocator, param);
            defer allocator.free(param_name);
            try appendPrint(out, allocator, "comptime {s}: type", .{param_name});
        }
        try append(out, allocator, ") type {\n    return struct {\n");
    }
    try append(out, allocator, "    const Self = @This();\n");
    try append(out, allocator, "    const Tag = enum(u32) { ");
    for (type_decl.variants, 0..) |variant, index| {
        if (index != 0) try append(out, allocator, ", ");
        const variant_name = try userVariantName(allocator, variant.name);
        defer allocator.free(variant_name);
        try appendPrint(out, allocator, "{s} = {d}", .{ variant_name, variant.tag });
    }
    try append(out, allocator, " };\n");

    try append(out, allocator, "    const Payload = union { ");
    var payload_count: usize = 0;
    for (type_decl.variants) |variant| {
        if (variant.payload_types.len == 0) continue;
        if (payload_count != 0) try append(out, allocator, ", ");
        const variant_name = try userVariantName(allocator, variant.name);
        defer allocator.free(variant_name);
        const payload_ty = try payloadTypeName(allocator, variant, type_decl.params);
        defer allocator.free(payload_ty);
        try appendPrint(out, allocator, "{s}: {s}", .{ variant_name, payload_ty });
        payload_count += 1;
    }
    if (payload_count == 0) try append(out, allocator, "_none: void");
    try append(out, allocator, " };\n");
    try append(out, allocator, "    tag: Tag,\n");
    try append(out, allocator, "    payload: Payload = undefined,\n\n");

    for (type_decl.variants) |variant| {
        try emitVariantConstructorHelper(out, allocator, variant, type_decl.params);
    }
    if (type_decl.params.len == 0) {
        try append(out, allocator, "};\n");
    } else {
        try append(out, allocator, "    };\n}\n");
    }
}

fn emitRecordType(out: *std.ArrayList(u8), allocator: std.mem.Allocator, type_decl: lir.LRecordType) EmitError!void {
    if (std.mem.eql(u8, type_decl.name, "account") and type_decl.params.len == 0) return;
    const type_name = try userTypeName(allocator, type_decl.name);
    defer allocator.free(type_name);
    if (type_decl.params.len == 0) {
        try appendPrint(out, allocator, "const {s} = struct {{\n", .{type_name});
    } else {
        try appendPrint(out, allocator, "fn {s}(", .{type_name});
        for (type_decl.params, 0..) |param, index| {
            if (index != 0) try append(out, allocator, ", ");
            const param_name = try typeParamName(allocator, param);
            defer allocator.free(param_name);
            try appendPrint(out, allocator, "comptime {s}: type", .{param_name});
        }
        try append(out, allocator, ") type {\n    return struct {\n");
    }
    for (type_decl.fields) |field| {
        try append(out, allocator, "    ");
        try emitIdentifier(out, allocator, field.name);
        try append(out, allocator, ": ");
        const ty_name = try zigTypeExprName(allocator, field.ty, type_decl.params);
        defer allocator.free(ty_name);
        try appendPrint(out, allocator, "{s},\n", .{ty_name});
    }
    if (type_decl.params.len == 0) {
        try append(out, allocator, "};\n");
    } else {
        try append(out, allocator, "    };\n}\n");
    }
}

fn emitClosureCaptureStructs(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    functions: []const lir.LFunc,
) EmitError!void {
    for (functions) |func| {
        if (func.calling_convention != .Closure or func.captures.len == 0) continue;
        const type_name = try closureCaptureStructName(allocator, func.name);
        defer allocator.free(type_name);
        try appendPrint(out, allocator, "const {s} = struct {{\n", .{type_name});
        for (func.captures) |capture| {
            try append(out, allocator, "    ");
            try emitIdentifier(out, allocator, capture.name);
            try append(out, allocator, ": ");
            const capture_ty = try zigTypeName(allocator, capture.ty);
            defer allocator.free(capture_ty);
            try appendPrint(out, allocator, "{s},\n", .{capture_ty});
        }
        try append(out, allocator, "};\n\n");
    }
}

fn emitVariantConstructorHelper(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    variant: lir.LVariantCtor,
    type_params: []const []const u8,
) EmitError!void {
    const variant_name = try userVariantName(allocator, variant.name);
    defer allocator.free(variant_name);

    try appendPrint(out, allocator, "    pub fn {s}(", .{variant_name});
    if (variant.payload_types.len == 1) {
        const payload_ty = try zigTypeExprName(allocator, variant.payload_types[0], type_params);
        defer allocator.free(payload_ty);
        try appendPrint(out, allocator, "value: {s}", .{payload_ty});
    } else if (variant.payload_types.len > 1) {
        const payload_ty = try payloadTypeName(allocator, variant, type_params);
        defer allocator.free(payload_ty);
        try appendPrint(out, allocator, "value: {s}", .{payload_ty});
    }
    try append(out, allocator, ") Self {\n");
    try appendPrint(out, allocator, "        return .{{ .tag = .{s}", .{variant_name});
    if (variant.payload_types.len > 0) {
        try appendPrint(out, allocator, ", .payload = .{{ .{s} = value }}", .{variant_name});
    }
    try append(out, allocator, " };\n");
    try append(out, allocator, "    }\n");
}

fn emitFunction(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    func: lir.LFunc,
    functions: []const lir.LFunc,
    type_decls: []const lir.LVariantType,
    record_type_decls: []const lir.LRecordType,
) EmitError!void {
    try append(out, allocator, "// source span: unavailable (M0 frontend bridge does not emit spans yet)\n");
    const is_entrypoint = std.mem.eql(u8, func.name, "entrypoint");
    try append(out, allocator, if (is_entrypoint) "pub inline fn " else "fn ");
    const function_name = switch (func.calling_convention) {
        .ArenaThreaded => try emittedFunctionName(allocator, func.name),
        .Closure => try emittedClosureFunctionName(allocator, func.name),
    };
    defer freeEmittedFunctionName(allocator, func.name, function_name);
    try append(out, allocator, function_name);
    try append(out, allocator, "(arena: *Arena");
    if (is_entrypoint) {
        try append(out, allocator, ", omlz_runtime_accounts: []AccountRuntime.AccountView, omlz_runtime_instruction_data: []const u8) u64 {\n");
    } else {
        if (func.calling_convention == .Closure) {
            try append(out, allocator, ", closure: *const prelude.Closure");
        }
        for (func.params) |param| {
            try append(out, allocator, ", ");
            try emitIdentifier(out, allocator, param.name);
            try append(out, allocator, ": ");
            const ty_name = try zigTypeName(allocator, param.ty);
            defer allocator.free(ty_name);
            try append(out, allocator, ty_name);
        }
        const return_ty = try zigTypeName(allocator, func.return_ty);
        defer allocator.free(return_ty);
        try appendPrint(out, allocator, ") {s} {{\n", .{return_ty});
    }
    const entrypoint_needs_account_list = is_entrypoint and paramsNeedEntrypointAccountList(func.params);
    if (!exprNeedsArena(func.body, type_decls) and !entrypoint_needs_account_list) {
        try append(out, allocator, "    _ = arena;\n");
    }
    if (is_entrypoint) {
        const bindings = try emitEntrypointRuntimeBindings(out, allocator, func.params, func.body);
        if (!bindings.accounts_used) try append(out, allocator, "    _ = omlz_runtime_accounts;\n");
        if (!bindings.instruction_data_used) try append(out, allocator, "    _ = omlz_runtime_instruction_data;\n");
    }
    if (func.calling_convention == .Closure) {
        try emitClosureCaptureBindings(out, allocator, func.name, func.captures);
    }
    try append(out, allocator, if (is_entrypoint) "    return @intCast(" else "    return ");
    var ctx: EmitContext = .{ .is_entrypoint = is_entrypoint, .functions = functions, .type_decls = type_decls, .record_type_decls = record_type_decls };
    try emitExpr(out, allocator, func.body, 1, &ctx);
    try append(out, allocator, if (is_entrypoint) ");\n" else ";\n");
    try append(out, allocator, "}\n");
}

fn emitBuiltinAccountRecordType(out: *std.ArrayList(u8), allocator: std.mem.Allocator) EmitError!void {
    try append(out, allocator,
        \\const omlz_type_account = struct {
        \\    key: []const u8,
        \\    lamports: i64,
        \\    data: []const u8,
        \\    owner: []const u8,
        \\    is_signer: prelude.Bool,
        \\    is_writable: prelude.Bool,
        \\    executable: prelude.Bool,
        \\};
        \\
    );
}

fn emitAccountViewHelper(out: *std.ArrayList(u8), allocator: std.mem.Allocator) EmitError!void {
    try append(out, allocator,
        \\fn omlz_log_account_view(view: AccountRuntime.AccountView) void {
        \\    const hex = "0123456789abcdef";
        \\    var key_hex: [64]u8 = undefined;
        \\    for (view.key.*, 0..) |byte, index| {
        \\        key_hex[index * 2] = hex[byte >> 4];
        \\        key_hex[index * 2 + 1] = hex[byte & 0x0f];
        \\    }
        \\    syscalls.sol_log_(key_hex[0..]);
        \\    syscalls.sol_log_64_(@intCast(view.lamportsValue()), 0, 0, 0, 0);
        \\}
        \\
    );
}

const EntrypointRuntimeBindings = struct {
    accounts_used: bool = false,
    instruction_data_used: bool = false,
};

fn emitEntrypointRuntimeBindings(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    params: []const lir.LParam,
    body: lir.LExpr,
) EmitError!EntrypointRuntimeBindings {
    var bindings: EntrypointRuntimeBindings = .{};
    var account_index: usize = 0;
    for (params) |param| {
        if (paramNeedsEntrypointInstructionData(param)) {
            bindings.instruction_data_used = true;
            try append(out, allocator, "    const ");
            try emitIdentifier(out, allocator, param.name);
            try append(out, allocator, " = omlz_runtime_instruction_data;\n");
            if (!exprUsesName(body, param.name)) {
                try append(out, allocator, "    _ = ");
                try emitIdentifier(out, allocator, param.name);
                try append(out, allocator, ";\n");
            }
            continue;
        }
        if (!paramNeedsEntrypointAccounts(param)) continue;
        bindings.accounts_used = true;
        if (isAccountTy(param.ty)) {
            try append(out, allocator, "    const ");
            try emitIdentifier(out, allocator, param.name);
            try appendPrint(out, allocator, " = if (omlz_runtime_accounts.len > {d}) omlz_runtime_accounts[{d}] else return 1;\n", .{ account_index, account_index });
            if (!exprUsesName(body, param.name)) {
                try append(out, allocator, "    _ = ");
                try emitIdentifier(out, allocator, param.name);
                try append(out, allocator, ";\n");
            }
            account_index += 1;
        } else {
            try append(out, allocator, "    for (omlz_runtime_accounts) |omlz_entry_account| omlz_log_account_view(omlz_entry_account);\n");
            try append(out, allocator, "    const ");
            try emitIdentifier(out, allocator, param.name);
            try append(out, allocator, " = @as(i64, 0);\n");
            if (!exprUsesName(body, param.name)) {
                try append(out, allocator, "    _ = ");
                try emitIdentifier(out, allocator, param.name);
                try append(out, allocator, ";\n");
            }
        }
    }
    return bindings;
}

fn emittedFunctionName(allocator: std.mem.Allocator, source_name: []const u8) EmitError![]const u8 {
    if (std.mem.eql(u8, source_name, "entrypoint")) {
        return "omlz_user_entrypoint";
    }

    var out = std.ArrayList(u8).empty;
    errdefer out.deinit(allocator);
    try append(&out, allocator, "omlz_user_");
    for (source_name) |ch| {
        if (std.ascii.isAlphanumeric(ch) or ch == '_') {
            try out.append(allocator, ch);
        } else {
            try out.append(allocator, '_');
        }
    }
    return out.toOwnedSlice(allocator);
}

fn emittedClosureFunctionName(allocator: std.mem.Allocator, source_name: []const u8) EmitError![]const u8 {
    const base = try emittedFunctionName(allocator, source_name);
    defer freeEmittedFunctionName(allocator, source_name, base);
    return std.fmt.allocPrint(allocator, "{s}__closure", .{base});
}

fn emitClosureCaptureBindings(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    func_name: []const u8,
    captures: []const lir.LParam,
) EmitError!void {
    if (captures.len == 0) try append(out, allocator, "    _ = closure;\n");
    if (captures.len == 0) return;

    const type_name = try closureCaptureStructName(allocator, func_name);
    defer allocator.free(type_name);
    try appendPrint(out, allocator, "    const omlz_captures: *const {s} = @ptrCast(@alignCast(closure.captures.?));\n", .{type_name});
    for (captures) |capture| {
        try append(out, allocator, "    const ");
        try emitIdentifier(out, allocator, capture.name);
        try append(out, allocator, " = omlz_captures.");
        try emitIdentifier(out, allocator, capture.name);
        try append(out, allocator, ";\n");
    }
}

const EmitContext = struct {
    next_block_id: usize = 0,
    is_entrypoint: bool = false,
    functions: []const lir.LFunc = &.{},
    type_decls: []const lir.LVariantType = &.{},
    record_type_decls: []const lir.LRecordType = &.{},
};

fn emitExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    expr: lir.LExpr,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    switch (expr) {
        .Constant => |constant| switch (constant) {
            .Int => |value| {
                // Emit integer constants as @as(i64, value) instead of bare
                // literals.  Bare integer literals are comptime_int in Zig;
                // when used inside a labeled block that contains runtime
                // control flow (e.g. if-then-else), the compiler rejects
                // them with "value with comptime-only type depends on
                // runtime control flow".  Wrapping in @as(i64, ...) gives
                // a concrete runtime type that works in all contexts.
                try appendPrint(out, allocator, "@as(i64, {d})", .{value});
            },
            .String => |value| try appendPrint(out, allocator, "\"{f}\"", .{std.zig.fmtString(value)}),
        },
        .Var => |var_ref| try emitIdentifier(out, allocator, var_ref.name),
        .App => |app| try emitAppExpr(out, allocator, app, indent_level, ctx),
        .Let => |let_expr| try emitLetExpr(out, allocator, let_expr, indent_level, ctx),
        .If => |if_expr| try emitIfExpr(out, allocator, if_expr, indent_level, ctx),
        .Prim => |prim| try emitPrimExpr(out, allocator, prim, indent_level, ctx),
        .Ctor => |ctor_expr| try emitCtorExpr(out, allocator, ctor_expr, indent_level, ctx),
        .Match => |match_expr| try emitMatchExpr(out, allocator, match_expr, indent_level, ctx),
        .Closure => |closure| try emitClosureExpr(out, allocator, closure, indent_level, ctx),
        .Tuple => |tuple_expr| try emitTupleExpr(out, allocator, tuple_expr, indent_level, ctx),
        .TupleProj => |tuple_proj| try emitTupleProjExpr(out, allocator, tuple_proj, indent_level, ctx),
        .Record => |record_expr| try emitRecordExpr(out, allocator, record_expr, indent_level, ctx),
        .RecordField => |record_field| try emitRecordFieldExpr(out, allocator, record_field, indent_level, ctx),
        .RecordUpdate => |record_update| try emitRecordUpdateExpr(out, allocator, record_update, indent_level, ctx),
        .AccountFieldSet => |field_set| try emitAccountFieldSetExpr(out, allocator, field_set, indent_level, ctx),
    }
}

fn emitTupleExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    tuple_expr: lir.LTuple,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    _ = tuple_expr.ty;
    try append(out, allocator, ".{ ");
    for (tuple_expr.items, 0..) |item, index| {
        if (index != 0) try append(out, allocator, ", ");
        try appendPrint(out, allocator, ".@\"{d}\" = ", .{index});
        try emitExpr(out, allocator, item.*, indent_level, ctx);
    }
    try append(out, allocator, " }");
}

fn emitTupleProjExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    tuple_proj: lir.LTupleProj,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    try append(out, allocator, "(");
    try emitExpr(out, allocator, tuple_proj.tuple_expr.*, indent_level, ctx);
    try appendPrint(out, allocator, ").@\"{d}\"", .{tuple_proj.index});
}

fn emitRecordExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    record_expr: lir.LRecord,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    if (isAccountTy(record_expr.ty)) return error.UnsupportedExpr;
    const ty_name = try zigTypeName(allocator, record_expr.ty);
    defer allocator.free(ty_name);
    try appendPrint(out, allocator, "{s}{{ ", .{ty_name});
    for (record_expr.fields, 0..) |field, index| {
        if (index != 0) try append(out, allocator, ", ");
        try append(out, allocator, ".");
        try emitIdentifier(out, allocator, field.name);
        try append(out, allocator, " = ");
        try emitExpr(out, allocator, field.value.*, indent_level, ctx);
    }
    try append(out, allocator, " }");
}

fn emitRecordFieldExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    record_field: lir.LRecordField,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    if (record_field.record_ty) |record_ty| {
        if (isAccountTy(record_ty)) {
            try emitAccountFieldReadExpr(out, allocator, record_field, indent_level, ctx);
            return;
        }
    }
    try append(out, allocator, "(");
    try emitExpr(out, allocator, record_field.record_expr.*, indent_level, ctx);
    try append(out, allocator, ").");
    try emitIdentifier(out, allocator, record_field.field_name);
}

fn emitAccountFieldReadExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    record_field: lir.LRecordField,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    if (std.mem.eql(u8, record_field.field_name, "lamports")) {
        try append(out, allocator, "@as(i64, @intCast((");
        try emitExpr(out, allocator, record_field.record_expr.*, indent_level, ctx);
        try append(out, allocator, ").lamportsValue()))");
        return;
    }
    if (std.mem.eql(u8, record_field.field_name, "data")) {
        try append(out, allocator, "(");
        try emitExpr(out, allocator, record_field.record_expr.*, indent_level, ctx);
        try append(out, allocator, ").data");
        return;
    }
    if (std.mem.eql(u8, record_field.field_name, "key")) {
        try append(out, allocator, "(");
        try emitExpr(out, allocator, record_field.record_expr.*, indent_level, ctx);
        try append(out, allocator, ").key[0..]");
        return;
    }
    if (std.mem.eql(u8, record_field.field_name, "owner")) {
        try append(out, allocator, "(");
        try emitExpr(out, allocator, record_field.record_expr.*, indent_level, ctx);
        try append(out, allocator, ").owner[0..]");
        return;
    }
    if (std.mem.eql(u8, record_field.field_name, "is_signer") or
        std.mem.eql(u8, record_field.field_name, "is_writable") or
        std.mem.eql(u8, record_field.field_name, "executable"))
    {
        try append(out, allocator, "prelude.Bool.fromNative((");
        try emitExpr(out, allocator, record_field.record_expr.*, indent_level, ctx);
        try append(out, allocator, ").");
        try emitIdentifier(out, allocator, record_field.field_name);
        try append(out, allocator, ")");
        return;
    }
    return error.UnsupportedExpr;
}

fn emitAccountFieldSetExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    field_set: lir.LAccountFieldSet,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    const block_id = ctx.next_block_id;
    ctx.next_block_id += 1;
    try appendPrint(out, allocator, "blk{d}: {{\n", .{block_id});
    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "const omlz_account_{d} = ", .{block_id});
    try emitExpr(out, allocator, field_set.account_expr.*, indent_level + 1, ctx);
    try append(out, allocator, ";\n");
    if (std.mem.eql(u8, field_set.field_name, "lamports")) {
        try emitIndent(out, allocator, indent_level + 1);
        try appendPrint(out, allocator, "omlz_account_{d}.lamports.* = @intCast(", .{block_id});
        try emitExpr(out, allocator, field_set.value.*, indent_level + 1, ctx);
        try append(out, allocator, ");\n");
    } else if (std.mem.eql(u8, field_set.field_name, "data")) {
        try emitIndent(out, allocator, indent_level + 1);
        try appendPrint(out, allocator, "const omlz_account_data_{d} = ", .{block_id});
        try emitExpr(out, allocator, field_set.value.*, indent_level + 1, ctx);
        try append(out, allocator, ";\n");
        try emitIndent(out, allocator, indent_level + 1);
        try appendPrint(out, allocator, "if (omlz_account_data_{d}.len > omlz_account_{d}.data.len) unreachable;\n", .{ block_id, block_id });
        try emitIndent(out, allocator, indent_level + 1);
        try appendPrint(out, allocator, "@memcpy(omlz_account_{d}.data[0..omlz_account_data_{d}.len], omlz_account_data_{d});\n", .{ block_id, block_id, block_id });
    } else if (std.mem.eql(u8, field_set.field_name, "key") or std.mem.eql(u8, field_set.field_name, "owner")) {
        try emitIndent(out, allocator, indent_level + 1);
        try appendPrint(out, allocator, "@compileError(\"ZXCAML: account field '{s}' is read-only\");\n", .{field_set.field_name});
    } else {
        try emitIndent(out, allocator, indent_level + 1);
        try appendPrint(out, allocator, "@compileError(\"ZXCAML: account field '{s}' is not writable\");\n", .{field_set.field_name});
    }
    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "break :blk{d};\n", .{block_id});
    try emitIndent(out, allocator, indent_level);
    try append(out, allocator, "}");
}

fn emitRecordUpdateExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    record_update: lir.LRecordUpdate,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    const record_ty = switch (record_update.ty) {
        .Record => |value| value,
        else => return error.UnsupportedExpr,
    };
    const record_decl = findRecordTypeDecl(ctx.record_type_decls, record_ty.name) orelse return error.UnsupportedExpr;
    const ty_name = try zigTypeName(allocator, record_update.ty);
    defer allocator.free(ty_name);
    const block_id = ctx.next_block_id;
    ctx.next_block_id += 1;
    try appendPrint(out, allocator, "blk{d}: {{\n", .{block_id});
    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "const omlz_record_base_{d} = ", .{block_id});
    try emitExpr(out, allocator, record_update.base_expr.*, indent_level + 1, ctx);
    try append(out, allocator, ";\n");
    try emitIndent(out, allocator, indent_level + 1);
    try appendPrint(out, allocator, "break :blk{d} {s}{{ ", .{ block_id, ty_name });
    for (record_decl.fields, 0..) |field, index| {
        if (index != 0) try append(out, allocator, ", ");
        try append(out, allocator, ".");
        try emitIdentifier(out, allocator, field.name);
        try append(out, allocator, " = ");
        if (findRecordUpdateField(record_update.fields, field.name)) |updated| {
            try emitExpr(out, allocator, updated.value.*, indent_level + 1, ctx);
        } else {
            try appendPrint(out, allocator, "omlz_record_base_{d}.", .{block_id});
            try emitIdentifier(out, allocator, field.name);
        }
    }
    try append(out, allocator, " };\n");
    try emitIndent(out, allocator, indent_level);
    try append(out, allocator, "}");
}

fn emitAppExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    app: lir.LApp,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!void {
    if (app.kind == .Closure) {
        const callee = switch (app.callee.*) {
            .Var => |var_ref| var_ref,
            else => return error.UnsupportedExpr,
        };
        const closure_ty = switch (app.callee_ty) {
            .Closure => |value| value,
            else => return error.UnsupportedExpr,
        };
        try append(out, allocator, "blk");
        const block_id = ctx.next_block_id;
        ctx.next_block_id += 1;
        try appendPrint(out, allocator, "{d}: {{\n", .{block_id});
        try emitIndent(out, allocator, indent_level + 1);
        try append(out, allocator, "switch (");
        try emitIdentifier(out, allocator, callee.name);
        try append(out, allocator, ".code) {\n");
        var emitted_case = false;
        for (ctx.functions, 0..) |func, index| {
            if (func.calling_convention != .Closure) continue;
            if (!closureFuncMatchesTy(func, closure_ty)) continue;
            emitted_case = true;
            try emitIndent(out, allocator, indent_level + 2);
            try appendPrint(out, allocator, "{d} => break :blk{d} ", .{ index + 1, block_id });
            const function_name = try emittedClosureFunctionName(allocator, func.name);
            defer allocator.free(function_name);
            try appendPrint(out, allocator, "{s}(arena, ", .{function_name});
            try emitIdentifier(out, allocator, callee.name);
            for (app.args) |arg| {
                try append(out, allocator, ", ");
                try emitExpr(out, allocator, arg.*, indent_level + 2, ctx);
            }
            try append(out, allocator, "),\n");
        }
        if (!emitted_case) return error.UnsupportedExpr;
        try emitIndent(out, allocator, indent_level + 2);
        try append(out, allocator, "else => unreachable,\n");
        try emitIndent(out, allocator, indent_level + 1);
        try append(out, allocator, "}\n");
        try emitIndent(out, allocator, indent_level);
        try append(out, allocator, "}");
        return;
    }

    switch (app.callee.*) {
        .Var => |callee| {
            if (try emitSyscallAppExpr(out, allocator, callee.name, app, indent_level, ctx)) return;
            if (try emitSplTokenAppExpr(out, allocator, callee.name, app, indent_level, ctx)) return;
            if (try emitStdlibAppExpr(out, allocator, callee.name, app, indent_level, ctx)) return;
            const function_name = try emittedFunctionName(allocator, callee.name);
            defer freeEmittedFunctionName(allocator, callee.name, function_name);
            try append(out, allocator, function_name);
        },
        else => return error.UnsupportedExpr,
    }
    try append(out, allocator, "(arena");
    for (app.args) |arg| {
        try append(out, allocator, ", ");
        try emitExpr(out, allocator, arg.*, indent_level, ctx);
    }
    try append(out, allocator, ")");
}

fn emitSyscallAppExpr(
    out: *std.ArrayList(u8),
    allocator: std.mem.Allocator,
    name: []const u8,
    app: lir.LApp,
    indent_level: usize,
    ctx: *EmitContext,
) EmitError!bool {
    if (std.mem.eql(u8, name, "Syscall.sol_log")) {
        if (app.args.len != 1) return error.UnsupportedExpr;
        if (try emitSyscallStringCallBlock(out, allocator, app.args[0].*, indent_level, ctx, "syscalls.sol_log_", false, false)) return true;
        try append(out, allocator, "syscalls.sol_log_(");
        try emitExpr(out, allocator, app.args[0].*, indent_level, ctx);
        try append(out, allocator, ")");
        return true;
    }
    if (std.mem.eql(u8, name, "Syscall.sol_log_64")) {
        if (app.args.len != 5) return error.UnsupportedExpr;
        try append(out, allocator, "syscalls.sol_log_64_(");
        for (app.args, 0..) |arg, index| {
            if (index != 0) try append(out, allocator, ", ");
            try emitExpr(out, allocator, arg.*, indent_level, ctx);
        }
        try append(out, allocator, ")");
        return true;
    }
    if (std.mem.eql(u8, name, "Syscall.sol_log_pubkey")) {
        if (app.args.len != 1) return error.UnsupportedExpr;
        try append(out, allocator, "syscalls.sol_log_pubkey(@ptrCast((");
        try emitExpr(out, allocator, app.args[0].*, indent_level, ctx);
        try append(out, allocator, ").ptr))");
        return true;
    }
    if (std.mem.eql(u8, name, "Syscall.sol_sha256")) {
        if (app.args.len != 1) return error.UnsupportedExpr;
        if (try emitSyscallStringCallBlock(out, allocator, app.args[0].*, indent_level, ctx, "syscalls.sol_sha256_alloc", true, true)) return true;
        try append(out, allocator, "syscalls.sol_sha256_alloc(arena, ");
        try emitExpr(out, allocator, app.args[0].*, indent_level, ctx);
        try append(out, allocator, ")");
        return true;
    }
    if (std.mem.eql(u8, name, "Syscall.sol_keccak256")) {
        if (app.args.len != 1) return error.UnsupportedExpr;
        if (try emitSyscallStringCallBlock(out, allocator, app.args[0].*, indent_level, ctx, "syscalls.sol_keccak256_alloc", true, true)) return true;
        try append(out, allocator, "syscalls.sol_keccak256_alloc(arena, ");
        try emitExpr(out, allocator, app.args[0].*, indent_level, ctx);
        try append(out, allocator, ")");
        return true;
    }
    if (std.mem.eql(u8, name, "Syscall.sol_get_clock_sysvar")) {
        if (app.args.len != 1) return error.UnsupportedExpr;
        try emitSyscallClockExpr(out, allocator, app, indent_level, ctx);
        return true;
    }
    if (std.mem.eql(u8, name, "Syscall.sol_remaining_compute_units")) {
        if (app.args.len != 1) return error.UnsupportedExpr;
        try append(out, allocator, "@as(i64, @intCast(syscalls.sol_remaining_compute_units()))");
        return true;
    }
    if (std.mem.eql(u8, name, "set_return_data") or std.mem.eql(u8, name, "Cpi.set_return_data") or std.mem.eql(u8, name, "Syscall.sol_set_return_data")) {
        if (app.args.len != 1) return error.UnsupportedExpr;
        if (try emitSyscallStringCallBlock(out, allocator, app.args[0].*, indent_level, ctx, "cpi.sol_set_return_data", false, false)) return true;
        try append(out, allocator, "cpi.sol_set_return_data(");
        try emitExpr(out, allocator, app.args[0].*, indent_level, ctx);
        try append(out, allocator, ")");
        return true;
    }
    if (std.mem.eql(u8, name, "get_return_data") or std.mem.eql(u8, name, "Cpi.get_return_data") or std.mem.eql(u8, name, "Syscall.sol_get_return_data")) {
        if (app.args.len != 1) return error.UnsupportedExpr;
        try append(out, allocator, "cpi.sol_get_return_data_alloc(arena)");
        return true;
    }
    if (std.mem.eql(u8, name, "invoke")) {
        try emitCpiInvoke(out, allocator, app, indent_level, ctx, false);
        return true;
    }
    if (std.mem.eql(u8, name, "invoke_signed")) {
        try emitCpiInvoke(out, allocator, app, indent_level, ctx, true);
        return true;
    }
    if (std.mem.eql(u8, name, "create_program_address")) {
        try emitCreateProgramAddress(out, allocator, app, indent_level, ctx);
        return true;
    }
    return false;
}

