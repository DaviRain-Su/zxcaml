fn concreteTypeParamName(
    allocator: std.mem.Allocator,
    name: []const u8,
    type_params: []const []const u8,
    concrete_ty: lir.LTy,
) EmitError![]const u8 {
    const adt = switch (concrete_ty) {
        .Adt => |value| value,
        else => return error.UnsupportedExpr,
